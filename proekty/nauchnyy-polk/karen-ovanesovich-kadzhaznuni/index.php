<p>
 <b>Карен Ованесович Каджазнуни&nbsp;(1906-1941)</b>
</p>
<p style="text-align: justify;">
	Карен Каджазнуни родился в Баку, но его семья сразу переехала в Тифлис, где и прошли его детство и юность, а затем, в 1925 году, в Ереван. Там Каджазнуни влёкся кинематографом и стал работать на «Арменкино», одновременно публикуя статьи о кинематографе.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Карен Каджазнуни работал вместе с ветераном кинорежиссуры Амо Бек-Назаровым, который дал ему рекомендацию для поступления в аспирантуру ВГИКа, на кафедру кинодраматургии. Учился он с большим интересом и продолжал публиковать статьи как в российской, так и в армянской прессе.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <i>Из писем К.О. Каджазнуни родным:</i>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 ... Лишь бы разбить Гитлера и сохранить жизнь и здоровье близких мне людей. Вот два моих желания; одно общее и другое личное. Для нас уничтожение фашизма - даже более важная задача, чам разгром Карфагена для римлян. В этом у меня нет никаких сомнений...
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 [...]
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 ... А вообще обо мне думать надо не в том смысле - убьют меня или нет, а наоборот, убью ли я фашистов или нет, и если да - то сколько штук...
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 [...]
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 ...Я же лично надеюсь, что придёт время, когда я тебе напишу письмо из Берлина, но не фашистского, а свободного.
</p>
