<p style="text-align: justify;">
 <b>Моисей Менделевич&nbsp;Сегаль&nbsp;(1915-2013), фронтовой кинооператор</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	Моисей Сегаль окончил операторский факультет ВГИКа в 1941 году – он был в числе 22-х студентов, получивших дипломы 28 июля, вскоре после начала войны.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Вскоре Сегаль ушёл на фронт добровольцем (место призыва —Ростокинский РВК Москвы) и с 5 августа 1941 года по 25 апреля 1942 года находился в рядах Красной Армии в звании ст. техника-лейтенанта, пройдя боевой путь в составе Ленинградского фронта.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 22 мая 1942 Моисей Сегаль стал ассистентом оператора в военной киногруппе Северо-Западного фронта, а в ноябре 1944 года — ассистентом оператора в киногруппе 2-го Прибалтийского фронта. Снятые им кадры вошли в хроникальные «Союзкиножурналы», в документальные фильмы 1944 года «Восьмой удар (боевые действия советских войск по освобождению Прибалтики)» и «Битва за Прибалтику», в фильм 1945 года «<a href="https://csdfmuseum.ru/films/194-%D0%BA%D0%B8%D0%BD%D0%BE%D0%B4%D0%BE%D0%BA%D1%83%D0%BC%D0%B5%D0%BD%D1%82%D1%8B-%D0%BE-%D0%B7%D0%B2%D0%B5%D1%80%D1%81%D1%82%D0%B2%D0%B0%D1%85-%D0%BD%D0%B5%D0%BC%D0%B5%D1%86%D0%BA%D0%BE-%D1%84%D0%B0%D1%88%D0%B8%D1%81%D1%82%D1%81%D0%BA%D0%B8%D1%85-%D0%B7%D0%B0%D1%85%D0%B2%D0%B0%D1%82%D1%87%D0%B8%D0%BA%D0%BE%D0%B2-18">Кинодокументы о зверствах немецко-фашистских захватчиков</a>» и другие.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Из монтажного листа кинолетописного документа 1944 года «К наступлению войск 2-го Прибалтийского фронта» (публикация Валерия Ивановича Фомина):
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 ...Операторами Я. (Якова Григорьевича) Марченко и М. Сегалем снято:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1.&nbsp;Артиллерия: огонь по противнику, смена огневых позиций под вражеским обстрелом.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2. Минометы в бою: минометный расчет из перелеска ведет огонь из 50-мм минометов.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3. Передний край: немецкая оборона, подбитые танки, окопы.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4. Ночью на передовой: сапёры, делающие проходы в немецкой обороне. Проход пехоты в сделанные сапёрами проходы. Атака взвода.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 5. Старая Русса. Общий план города с воздуха. Планы города, разрушенного немцами. Домик Достоевского. Курорт, фонтан источника, грязелечебница, санаторий и т. д...
</p>
<p style="text-align: justify;">
	<br>
