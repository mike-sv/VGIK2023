<p>
 <b>Давид Владимирович Файнштейн (1901-1941)</b>
</p>
<p style="text-align: justify;">
	Осенью 1940 года Давид Файнштейн был переведен из Киева на работу в Москву директором Всесоюзного государственного института кинематографии и вскоре стал считаться лучшим среди директоров московских вузов.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В начале июля 1941 года вместе со студентами, преподавателями и сотрудниками института (всего — 70 человек) Файнштейн записался добровольцем в 13-ю Ростокинскую дивизию народного ополчения. Он служил помощником командира взвода, политруком 38-го стрелкового полка дивизии, участвовал в сооружении Можайской линии обороны.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 До конца сентября 1941 года 13-я Ростокинская дивизия (в составе 32-й армии Резервного фронта) находилась во втором эшелоне. 26 сентября она была преобразована в 140-ю стрелковую дивизию и с 2 октября в составе регулярных войск в течение недели держала оборону по левому берегу реки Днепр северо-западнее Вязьмы.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Давид Файнштейн погиб в октябре 1941 года в окружении под Вязьмой.
</p>
