<div style="text-align: justify;">
	Порой кажется, что «оркестр» - это кухня где-нибудь в московской, питерской или киевской коммуналке, где хозяйки судачат о еде, мужьях, любовниках, погоде. Где всегда занят общественный туалет…
</div>
<div style="text-align: justify;">
	Звон посуды, звяканье ножей, вилок, бульканье супа в кастрюльках, дребезжание крышек… Кухонный оркестр.
</div>
<div style="text-align: justify;">
	Но! Вспыхивает свет, и кухня превращается в маленький подиум, на котором в хороводе кружатся наши героини: Сюзанна, Патриция, Леона, Памела, Эрмелина, Мадам Ортанс, мсье Леон. А может быть, Нина, Маша, Вера Петровна, Сергей Николаевич… Уже нет вилок, ножей, кастрюль, фартуков. Смычки, кружевные воротнички, розы в петлицах. Сцена в кафе санатория для желудочных больных становится ареной событий. Но разговоры остаются прежними, а французский так похож на русский…
</div>
<div style="text-align: justify;">
	А музыка?.. Бесконечный танец с бесчисленными репризами, из которых, кажется, не вырваться. Скрипки и альт, виолончель, контрабас и фортепиано. Шесть женщин и один мужчина. Семь персонажей в поисках выхода. И у каждого есть тайна, которая спрятана где-то глубоко под сердцем. Тайна печальная, нелепая, страшная, чувственная, смешная… Но есть миг, когда тайна откроется зрителю и каждый персонаж станет близким, понятным и чем-то похожим на нас.
</div>
<div style="text-align: justify;">
	Выстрел обрывает надоевший мотив, но не ставит точку. Пауза наполнится новой музыкой, и уже небесный оркестр играет страстно и нежно танго бессмертного Пьяццолы.
</div>
