	<td colspan="1">
 <b style="text-align: justify;">Руководитель актерской мастерской:</b><span style="text-align: justify;">&nbsp;</span><b style="text-align: justify;">Михайлов Александр Яковлевич</b><span style="text-align: justify;">, Народный артист РФ.</span>
		<p style="text-align: justify;">
			 В 1969 году окончил театральный факультет Дальневосточного педагогического института. И в этом же году стал артистом Приморского краевого драматического театра им. Горького, где дебютировал ролью Раскольникова в спектакле «Преступление и наказание».
		</p>
		<p style="text-align: justify;">
			 В 1970-1979 гг. работал в Саратовском академическом театре драмы им. К.Маркса.
		</p>
		<p style="text-align: justify;">
			 На киноэкране дебютировал главной ролью в фильме «Это сильнее меня» (1973, режиссёр Ф.Филиппов).
		</p>
