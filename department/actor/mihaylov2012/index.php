		<p>
			 В 1981 году «за талантливое воплощение образов современников в кино и высокое исполнительское мастерство» А. Я. Михайлов был удостоен премии Ленинского комсомола. Зрительский успех (лучший актёр 1982 года по результатам опроса журнала «Советский экран») и Государственную премию РСФСР имени братьев Васильевых (1983 г.) Михайлову принесла роль Павла Зубова в картине «Мужики!..» (1981 год, режиссёр - И.Бабич).
		</p>
		<p>
			 В 1973-1985 гг. играл в Московском театре им. Ермоловой.
		</p>
		<p>
			 С 1985 года - актер Государственного Академического Малого театра.
		</p>
		<p>
 <span style="font-weight: bold;">На курсе преподают:</span> Михайлов Александр Яковлевич, Арькова Елена Борисовна, Федоров Александр Львович, Акулова Тамара Васильевна.
