<p style="text-align: justify;">
	 Художественный факультет ВГИК был основан в 1938 году известным художником Ф.С. Богородским совместно с Б.В. Дубровским-Эшке, в то время - главным художником Мосфильма и И.А. Шпинелем, работавшим с С.М. Эйзенштейном над его шедеврами «Александр Невский» и «Иван Грозный». Мастерскую по подготовке художников мультипликационного фильма возглавил блестящий аниматор И.П. Иванов-Вано, один из зачинателей отечественного графического мультипликационного кино.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Традиции академического рисования, которые изначально были заложены в основу образования кинохудожников - залог высокого международного авторитета художественной школы ВГИК. Самое важное в работе факультета то, что его воспитанники не просто специалисты-профессионалы, но яркие творческие личности, живущие и работающие в широком контексте художественной культуры. Многие выпускники факультета были удостоены почетного звания народного художника РСФСР; около восьмидесяти - звания заслуженный деятель искусств и заслуженный художник республики. Десятки фильмов, созданных при участии воспитанников художественного факультета ВГИК, были отмечены высокими государственными наградами России, премиями Международных кинофестивалей в Каннах и Венеции. Факультет готовит специалистов для работы на киностудиях, в телекомпаниях, в фирмах, создающих мультимедийную продукцию.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Занятия по специальным дисциплинам проводятся в творческих мастерских, расположенных на художественном факультете и оборудованных с учетом специфики – рисунок и живопись, а также мастерство художника фильма.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 На факультете существует обширный методический фонд, насчитывающий более 10 000 работ, собранных за период с 1938 года. Факультет регулярно принимает участие в ретроспективных выставках педагогов и студентов как в России, так и за рубежом с использованием работ методического фонда, а также работ, выполненных за текущий период. Кроме того, учебные и творческие работы художественного факультета постоянно экспонируются в стенах университета. Огромную ценность представляют уникальные картины художников-классиков отечественного искусства - Левенталя, Пименова, Богородского, Гроссе и других.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Художественный факультет имеет большой реквизитный фонд, который используется как для обучения студентов, так и для съемочного процесса.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 На первом, втором и третьем курсе учебным планом предусмотрены учебно-творческие пленэрные практики с выездом в различные города России. Подобная традиция существует на протяжении всех 75 лет, то есть с момента основания художественного факультета. Пленэрная практика проходит 8 недель под руководством педагогов, и ее результатом становятся творческие работы, выполненные в различных городах – этюды, рисунки, зарисовки, станковые живописные работы по собранным материалам.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Система мастерских на художественном факультете устроена таким образом, что работа на кафедре мастерства художника фильма и кафедре рисунка и живописи проходит под руководством педагога, набравшего курс.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Студенты художественного факультета принимают активное участие в учебных кинофильмах вместе со студентами режиссерского и операторского факультета, участвуют в создании учебных фильмов не только на учебной киностудии университета, но и на ведущих киностудиях Москвы.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Художественный факультет входит в Учебно-методическое объединение высших учебных заведений Российской Федерации по образованию в области изобразительного искусства.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 При художественном факультете постоянно работают подготовительные курсы, на которых преподают ведущие педагоги по рисунку, живописи, композиции и истории искусства. Все желающие получить художественное образование могут выбрать одно из трех направлений: "Художник кино и ТВ", "Художник кино и ТВ по костюму" или "Художник мультипликационного фильма".&nbsp;
</p>
