<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Сегодня Россия взяла курс на инновационный прорыв. <a href="https://xn--80aapampemcchfmo7a3c9ehj.xn--p1ai/projects/nauka-i-universitety">Национальный проект «Наука и университеты»</a> реализуется согласно указам Президента Российской Федерации от 7 мая 2018 г. № 204 «О национальных целях и стратегических задачах развития Российской Федерации на период до 2024 года» и от 21 июля 2020 г. № 474 «О национальных целях развития Российской Федерации на период до 2030 года». Его ключевая задача — воспитание и поддержка нового поколения ученых, способных совершать великие открытия.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В рамках нацпроекта «Наука и университеты» выделяются значительные средства на поддержку ученых и создание научных центров с первоклассными условиями для исследовательской работы. Кроме этого, проводится работа по строительству и модернизации научно-исследовательского флота, совершенствованию цифровой инфраструктуры, а также созданию комфортных условий для школьников, студентов, научных работников и педагогов.
</p>
