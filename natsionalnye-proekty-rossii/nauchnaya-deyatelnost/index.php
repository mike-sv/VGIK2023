<p style="text-align: justify;">
</p>
<ul>
	<li>Научно-исследовательский центр кинообразования и экранных искусств, осуществляющий координацию научно-исследовательской, научно-методической, научно-организационной деятельности; </li>
	<li>Отдел докторантуры и аспирантуры; </li>
	<li>Библиотека вуза; </li>
	<li>Редакция печатного научного информационно-аналитического журнала «Вестник ВГИК»; </li>
	<li>Редакционно-издательский отдел (РИО); </li>
	<li>Совет по защите диссертаций на соискание ученой степени кандидата наук, на соискание ученой степени доктора наук (Д210.023.01). </li>
