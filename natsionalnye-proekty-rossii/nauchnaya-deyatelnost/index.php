	Кинематограф в системе искусств и наук. Личность и социальные модели в кино: fiction и non-fiction. Международная молодежная научно-практическая конференция; </li>
	<li>
	Творческое наследие Андрея Тарковского. Актуальные аспекты. К 90-летию режиссера. Международная научно-практическая конференция; </li>
	<li>
	Экзистенциализм и его репрезентации в литературе и кино. Посвящается 200-летнему юбилею Ф.М. Достоевского. XIII Международная научная конференция по эстетике экранизации; </li>
	<li>
