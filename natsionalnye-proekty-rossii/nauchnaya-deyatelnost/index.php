<p style="text-align: justify;">
	 Кафедры ВГИК - киноведения, драматургии кино, продюсерского мастерства, дистрибуции и маркетинга, эстетики, истории и теории культуры, истории и философии - формируют научные школы вуза, осуществляющие научную деятельность в области аудиовизуальной сферы и экранной культуры.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Исследователи занимаются проблемными вопросами теории и истории кинематографа и других экранных искусств, эстетическими и социокультурными особенностями современной аудиовизуальной сферы, общими проблемами теории искусств, экономическими, управленческими решениями и новыми технологиями в аудиовизуальной области, драматургией в кино, теорией литературы (в том числе теорией экранизации). Приоритетное место принадлежит междисциплинарным исследованиям на основе комплексного подхода к изучению актуальных вопросов современного научного знания в аудиовизуальной области на базе искусствоведческих, философских (эстетических), культурологических и экономических наук.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Студенческое научно-творческое общество, основным направлением работы которого является организация, поддержка и развитие НИР студентов и аспирантов ВГИК, действует в рамках приоритетных научных направлений, выработанных вузом: «Искусствоведение», «Философские науки», «Экономические науки».
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Особое внимание в научно-исследовательских работах уделяется&nbsp;&nbsp; сохранению исторической памяти, исследованию образа Великой Отечественной войны на экране, памятным датам (оставившим свой след в культуре России), легендарным Педагогам и Студентам ВГИК.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Исследования в области теории и истории кинематографа, в том числе анимации и мультимедиа, а также классического и современного обучения находят отражение на международных, всероссийских, межвузовских конференциях, проводимых ВГИК. В 2022 г. конференции имели следующую тематику:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul>
