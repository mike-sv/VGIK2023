	<li>
	Экранные искусства в контексте современных образовательных процессов. Международная научно-практическая конференция; </li>
	<li>
	Кино стран СНГ: история и перспективы развития. Международный круглый стол; </li>
	<li>
	Образование СССР как стимул развития национальных кинематографий современного СНГ. Международная научно-практическая конференция; </li>
	<li>
	Анимация как феномен культуры. Международная научно-практическая конференция; </li>
	<li>
	Ускользающая реальность: драматургия иллюзорного. Памяти Леонида Нехорошева. IX Научно-практическая конференция; </li>
	<li>
	Современные технологии аудиовизуального искусства. Межвузовская студенческая конференция; </li>
	<li>
	«И мир увидел Сталинград». Отражение Сталинградской битвы в отечественном и мировом кинематографе. Научная конференция; </li>
	<li>
