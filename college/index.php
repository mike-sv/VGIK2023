		<p style="text-align: justify;">
 <b><u style="text-align: justify; line-height: 150%; text-indent: 35.45pt;">Колледж кино, телевидения и мультимедиа ВГИК,</u></b><span style="text-align: justify; line-height: 150%; text-indent: 35.45pt;"> созданный по решению Ученого совета ВГИК в мае 2004 года, является структурным подразделением Всероссийского государственного&nbsp;</span>институт<span style="text-align: justify; line-height: 150%; text-indent: 35.45pt;">а кинематографии имени С.А. Герасимова, реализующим программы среднего профессионального образования. Взаимодействие с факультетами и кафедрами&nbsp;</span>институт<span style="text-align: justify; line-height: 150%; text-indent: 35.45pt;">а позволяет переносить и внедрять в Колледже многие вузовские традиции, придавать учебному процессу вузовскую ориентацию.&nbsp;</span>
		</p>
		<p style="text-align: justify;">
 <span style="text-indent: 35.4pt;">Преподаватели Колледжа – профессора, ведущие специалисты в области изобразительного искусства, анимации, преподаватели и аспиранты&nbsp;</span><span style="line-height: normal; text-align: start;">институт</span><span style="text-indent: 35.4pt;">а. &nbsp;</span>
		</p>
		<p style="text-align: justify;">
 <span style="text-indent: 35.45pt;">Создание единого творческого пространства на протяжении всего времени обучения, главной ценностью которого является каждый студент, формирует&nbsp; духовно-развитую, творческую, нравственно и физически здоровую личность, ориентированную на сознательный выбор жизненной позиции, самостоятельную выработку идей на современном уровне.</span>
		</p>
		<p style="text-align: justify;">
 <span style="text-indent: 35.45pt;">Хорошие знания и навыки получают студенты в ходе проведения учебной и производственной практики, направленной на формирование у студентов практических профессиональных умений, приобретение первоначального опыта,&nbsp; освоение общих и профессиональных компетенций по избранной специальности.&nbsp;</span>
		</p>
		<p style="text-align: justify;">
 <span style="text-indent: 35.45pt;">Прием в Колледж проводится как на бюджетной, так и на компенсационной основе. Прием за счет бюджетных ассигнований&nbsp; федерального бюджета&nbsp; проводится в соответствии с контрольными цифрами приема, утверждаемыми Министерством культуры РФ.&nbsp;</span>
		</p>
		<p style="text-align: justify;">
 <span style="text-indent: 35.4pt;">Колледж обучил и выпустил более 500 специалистов среднего звена для кинопроизводства, телевидения, анимационных студий, рекламных агентств и театров по следующим специальностям: «Живопись», «Техника и искусство фотографии», «Аудиовизуальная техника и звукотехническое обеспечение аудиовизуальных программ», «Светорежиссура», «Менеджмент», «Реклама»,&nbsp; «Театральная и аудиовизуальная техника». <br>
 </span>
		</p>
		<p style="text-align: justify;">
 <span style="text-indent: 35.4pt;">В настоящее время обучение проводится по специальности "Живопись (по видам)".<br>
 </span>
		</p>
		<p style="text-align: justify;">
 <span style="text-indent: 35.45pt;">Анализ качества образовательной деятельности показывает, что структура подготовки отвечает потребностям рынка в специалистах среднего звена.&nbsp;</span>
		</p>
		<p style="text-align: justify;">
 <span style="text-indent: 35.45pt;">Колледж оказывает дополнительные образовательные услуги – работают курсы профориентации.&nbsp;</span><span style="text-indent: 35.45pt;">Колледж кино, телевидения и мультимедиа ВГИК имени С.А.Герасимова дает хорошее практико-ориентированное образование - школьник имеет возможность после 9 класса завершить среднее образование и получить хорошую современную специальность, востребованную на рынке труда.</span>
		</p>
