<h2>Декабрь</h2>

<p align="justify">Сегодня университет – многопрофильное учебное заведение высшего и послевузовского профессионального образования Российской Федерации. Здесь готовят специалистов всех основных творческих профессий кино и телевидения на семи факультетах: актерском, режиссерском, сценарно-киноведческом, операторском, художественном, анимации и мультимедиа, продюсерства и экономики, в Центре дополнительного профессионального образования и в Колледже кино, телевидения и мультимедиа.</p>

<p align="justify">В университете работает система учебных творческих мастерских, как правило, по 5-20 студентов в каждой. Личность мастера, его профессиональный авторитет, стиль и педагогические приемы в решающей степени определяют специфику вгиковского образования.</p>

<p align="justify">ВГИК готовит специалистов кино, видео, телевидения и других экранных искусств по следующим направлениям:</p>

