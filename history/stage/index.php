<p align="justify">Во ВГИКе проходит выставка, посвященная 100-летию классика мировой анимации и основоположника преподавания анимации во ВГИКе проф И.П.Иванова-Вано.</p>

<p align="justify">На XX фестивале студенческих фильмов приз «За лучший игровой фильм» вручается У.Шилкиной («Ничего страшного»). Фильм отмечен Призом кинофестиваля «Литература и кино» в Гатчине «За лучший режиссерский дебют», Второй премией конкурса «Святая Анна».</p>

