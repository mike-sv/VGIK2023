<p align="justify"><i>1969 год.</i></p>

<p align="justify">К полувековому юбилею за заслуги в деле подготовки творческих кадров советского киноискусства ВГИК награжден орденом Трудового Красного Знамени.</p>

<p align="justify">Проведен VI фестиваль студенческих фильмов. Лучшим признан фильм С.Дружининой и В.Попова «Зинка».</p>

