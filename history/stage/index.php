<p align="justify">Впервые на фестиваль студенческих фильмов приезжают зарубежные гости: Государственная высшая школа кино, телевидения и театра в Лодзи (Польша), Высшая школа кинематографии и телевидения ГДР. В жюри входят: С.Ростоцкий, Ж.Болотова, В.Лисакович, Э.Рязанов, В.Тихонов, Л.Соколова. Н.Фатеева и др.</p>

