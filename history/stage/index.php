<p align="justify">В распоряжении студентов фонды научно- методических кабинетов, библиотеки, фильмотеки и видеотеки, парк съемочной аппаратуры…</p>

<p align="justify">ВГИК – единственная в мире кинощкола, которая располагает учебной киностудией с полным циклом кинопроизводства.</p>

<p align="justify">Университет не только учебный, но и научный центр. На 17 кафедрах и в аспирантуре ведутся научные исследования, защищаются кандидатские и докторские диссертации.</p>

<p align="justify">Здесь с учетом реформирования образования в рамках Болонской конвенции разрабатываются госстандарты и программы по творческим специальностям кино и телевидения.</p>

<p align="justify">Выпускники ВГИКа работают более чем в 90 странах мира: снимают кино- и телефильмы, преподают в киношколах, руководят государственными учреждениями культуры.</p>

