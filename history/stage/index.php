    <li>звукорежиссура кино и телевидения </li>
  
    <li>киноведение </li>
  
    <li>литературное творчество </li>
  
    <li>драматургия </li>
  
    <li>кинооператорство </li>
  
    <li>живопись </li>
  
    <li>графика </li>
  
    <li>менеджмент организации </li>
  
