<h2>80-е Сентябрь</h2>

<p align="justify"><i>1981 год.</i></p>

<p align="justify">На XIII фестивале студенческих фильмов приз за разработку нравственной темы в фильме получает фильм А.Эшпая «Звана».</p>

<p align="justify"><i>1982 год.</i></p>

<p align="justify">XIV фестиваль студенческих фильмов. Приз за мультипликационный фильм получает А.Петров (фильм «Зеркало»). Приз за творческий поиск, оригинальное решение фильма вручается И.Квирикадзе (фильм «Экскурсия»).</p>

<p align="justify"><i>1985 год.</i></p>

<p align="justify">XV фестиваль студенческих фильмов. Первую премию получает фильм С.Мирошниченко «Вот и вся жизнь»</p>

<p align="justify">Студенческие фильмы ВГИКа ежегодно включаются в программы крупнейших международных, российских кинофорумов и региональных фестивалей.</p>

<p align="justify"><i>1986 год.</i></p>

<p align="justify">ВГИКу присвоено имя С.А.Герасимова. Сорок лет жизни Сергея Герасимова связаны со ВГИКом, с мастерской, которую он вел совместно с Тамарой Макаровой. С 1944г. они подготовили 8 выпусков, 106 актеров и 108 режиссеров – советских и иностранных.</p>

<p align="justify"><i>Октябрь</i> <i>1986 года.</i></p>

<p align="justify">На всех пяти факультетах ВГИКа проходят «круглые столы» и вслед за ними – общеинститутская конференция, на которой подводятся итоги дискуссий о будущем ВГИКа, о методах обучения в главном киновузе страны.</p>

<p align="justify"><i>1989 год.</i></p>

<p align="justify">1 сентября ВГИК отмечает 70-летие со дня основания. В государственном концертном зале «Россия» проходит торжественная встреча, посвященная юбилею.</p>

<p align="justify">В сентябре ВГИК с участием Совета по проблемам высшего кинообразования СК СССР проводит международный симпозиум по теме «Кинообразование и национальная культура». ВГИК принимает представителей киношкол Венгрии, США, Англии, Франции, Индии, Финляндии и других стран.</p>

<h2>90-е Октябрь </h2>

<p align="justify">В Париже и Пуатье проходит фестиваль кинофильмов ВГИКа, в котором принимают участие дипломные работы выпускников ВГИКа, снятые в 1921-1993 годах.</p>

<p align="justify"><i>1991 год.</i></p>

<p align="justify">Создается первая мастерская режиссуры компьютерной графики под руководством В.Кобрина</p>

<p align="justify"><i>1994 год.</i></p>

<p align="justify">ВГИК отмечает 100-летие со дня рождения А.Довженко.</p>

<p align="justify"><i>1995 год.</i></p>

<p align="justify">Создается кафедра анимации и компьютерной графики под руководством С.Соколова.</p>

<p align="justify">На художественном факультете открывается первая мастерская анимации и компьютерной графики под руководством С.Соколова и Л.Носырева.</p>

<p align="justify"><i>1998 год.</i></p>

<p align="justify">21 января отмечается 100-летие со дня рождения С.Эйзенштейна.</p>

<p align="justify">Во ВГИКе создается факультет дополнительно профессионального образования. Обучение ведется по специальностям «Кинодраматургия». «Кинорежиссура», «Киноведение», «Продюсер кино и телевидения».</p>

<p align="justify">18 февраля во ВГИКе проходит церемония вручения диплома о присуждении звания «Почетный доктор ВГИК им. С.А.Герасимова» польскому кинорежиссеру К.Занусси.</p>

<p align="justify"><i>1999 год.</i></p>

<p align="justify">ВГИК отмечает свой 80-летний юбилей и 100-летие со дня рождения Льва Кулешова.</p>

<p align="justify">На XIX фестивале студенческих фильмов Главный приз имени Льва Кулешова присуждается фильмам «Плач» (реж. О.Борщ) и «Телефон» (реж. Е.Виснапу). Приз «За режиссуру игрового фильма» - Ю. Кузину («Левша»).</p>

<p align="justify">В марте 1998 года во ВГИКе открывается факультет дополнительного профессионального образования для творчески одаренных людей, имеющих высшее образование. Один из ведущих отечественных драматургов В. Черных набирает первую сценарную мастерскую. Затем формируется продюсерская мастерская под руководством В. Сидоренко, П. Огурчикова, киноведческая, которую возглавляет А. Трошин, первая режиссерская мастерская известного кинорежиссера Геннадия Полоки.</p>

<p align="justify">В педагогический коллектив входят драматурги О. Агишев, А. Бородянский, режиссеры В. Лонской, В. Ахадов, В. Фокин, С. Мирошниченко, Т. Семенов, В. Ман, М. Бабак, киновед И. Шилова.</p>

