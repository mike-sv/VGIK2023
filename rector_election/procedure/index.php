<p class="MsoNormal" align="center" style="text-align: center; "><b>1. Общие положения</b></p>
 
<p style="text-align: justify; "><b> 1.1.</b> Выборы ректора федерального государственного бюджетного образовательного учреждения высшего и послевузовского профессионального образования «Всероссийский государственный университет кинематографии имени С.А. Герасимова» (далее &ndash; Университет) проводятся в соответствии с Федеральным законом от 22.08.1996 года №125-ФЗ «О высшем и послевузовском профессиональном образовании», Постановлением Правительства Российской Федерации от 14.02.2008 г. № 71 «Об утверждении типового положения об образовательном учреждении высшего профессионального образования (высшем учебном заведении)», письмом Рособразования от 21.09.2006 г. № 18-02-10/08 «О процедуре избрания ректоров вузов», письмом Минобразования РФ «О требованиях к кандидатам при избрании на должность ректора вуза» от 15.07.2003 г. № 25-90ин/25-19, приказом Минкультуры России от 19.08.2008 г. № 52 «Об образовании комиссии по аттестации кандидатов на должности ректоров федеральных государственных образовательных учреждений высшего профессионального образования, подведомственных Министерству культуры Российской Федерации», письмом Министерства культуры Российской Федерации от 19 августа 2011 г. № 1619-06-05 «О рекомендациях по подготовке и проведению выборов ректоров в подведомственных Минкультуры России образовательных учреждениях высшего профессионального образования и продлении срока их пребывания в должности», Уставом Университета и настоящим Положением. </p>
 
<p style="text-align: justify; "><b> </b><b>1.2.</b> Ректор Университета избирается на Общем собрании (конференции) научно-педагогических работников, представителей других категорий работников и обучающихся Университета (далее – Конференция) по результатам обсуждения программ кандидатов на должность ректора Университета, согласованных с Комиссией по аттестации кандидатов на должность ректоров федеральных государственных образовательных учреждений высшего профессионального образования, подведомственных Министерству культуры Российской Федерации (далее – Комиссия по аттестации), тайным голосованием на срок до 5 лет. </p>
 
<p style="text-align: justify; "><b> 1.3.</b> Выборы ректора назначаются по согласованию с Министерством культуры Российской Федерации. </p>
 
<p> </p>
 
<p align="center"><b>2. Квалификационные требования к кандидатам на должность ректора </b></p>
 
<p style="text-align: justify; "><b> 2.1.</b> На должность ректора может быть избран кандидат, соответствующий следующим квалификационным требованиям: </p>
 
<p style="text-align: justify; ">– гражданин Российской Федерации; </p>
 
<p style="text-align: justify; ">– имеющий высшее профессиональное образование; </p>
 
<p style="text-align: justify; ">– в возрасте не старше 65 лет. </p>
 
<p style="text-align: justify; "> На должность ректора может быть избран кандидат как работающий, так и не работающий в Университете. </p>
 
<p style="text-align: justify; "><b> 2.2</b><b>.</b> Совмещение должности ректора с другой оплачиваемой руководящей должностью (кроме научного, научно-методического, творческого руководства) внутри или вне Университета не разрешается. Ректор не может исполнять свои обязанности по совместительству. </p>
 
<p style="text-align: justify; "> <b>2.3</b>. Установление каких-либо иных требований к кандидатам на должность ректора, ограничивающих конституционное право граждан на труд, не допускается. </p>
 
<p style="text-align: justify; "><b> 2.3</b>. Кандидаты на должность ректора до выборов на Конференции проходят аттестацию в Комиссии по аттестации. Для проведения аттестации председатель Комиссии по подготовке и проведению выборов направляет в Комиссию по аттестации заявления и документы кандидатов на должность ректора не позднее, чем за 11 дней до даты очередного заседания Комиссии по аттестации. После получения результатов аттестации Комиссия по подготовке и проведению выборов доводит их до сведения коллектива Университета. </p>
 
<p><b> </b> </p>
 
<p align="center"><b>3. Полномочия Ученого совета и комиссии по подготовке и проведению выборов </b></p>
 
<p style="text-align: justify; "><b> 3.1.</b> В целях проведения выборов ректора Ученый совет путем открытого голосования: </p>
 
<p style="text-align: justify; ">- назначает дату проведения Конференции по выборам ректора; </p>
 
<p style="text-align: justify; ">- принимает Положение о процедуре проведения выборов ректора; </p>
 
<p style="text-align: justify; ">- формирует Комиссию по подготовке и проведению выборов ректора и утверждает ее состав, назначает председателя Комиссии, заместителей председателя и секретаря Комиссии; </p>
 
<p style="text-align: justify; ">- утверждает порядок выдвижения кандидатур на должность ректора и сроки их представления в Комиссию по выборам ректора; </p>
 
<p style="text-align: justify; ">- определяет нормы представительства, порядок и сроки выборов делегатов на конференцию; </p>
 
<p style="text-align: justify; ">- утверждает список кандидатов на должность ректора. </p>
 
<p style="text-align: justify; "><b> 3.2.</b> Комиссия по подготовке и проведению выборов ректора (далее – Комиссия): </p>
 
<p style="text-align: justify; ">- действует с момента избрания ее Ученым советом до окончания выборов ректора и один месяц после выборов ректора; </p>
 
<p style="text-align: justify; ">- направляет документы для согласования даты выборов в Министерство культуры Российской Федерации; </p>
 
<p style="text-align: justify; ">- объявляет на сайте Университета о выборах ректора, сроках выборов, сроках подачи документов и требованиях к претендентам на должность ректора; </p>
 
<p style="text-align: justify; ">- размещает в СМИ объявления о проведении выборов ректора и дате проведения Конференции в течение 1 недели с даты согласования решения Ученого совета о проведении выборов с Министерством культуры Российской Федерации; </p>
 
<p style="text-align: justify; ">- дает разъяснения кандидатам на должность ректора и всем членам коллектива Университета по вопросам выборов, выдвижения кандидатов, документам; </p>
 
<p style="text-align: justify; ">- принимает документы о выдвижении кандидатов на должность ректора и о поддержке выдвижения кандидатов, проверяет правильность заполнения этих документов, осуществляет непосредственную работу с кандидатами; </p>
 
<p style="text-align: justify; ">- принимает документы от кандидатов, подтверждающие их квалификационные характеристики, проверяет подлинность этих документов; </p>
 
<p style="text-align: justify; ">- в случае несоответствия кандидатов квалификационным требованиям отказывает кандидатам в регистрации с письменным уведомлением кандидатов об этом; </p>
 
<p style="text-align: justify; ">- утверждает график выдвижения структурными подразделениями делегатов на Конференцию; </p>
 
<p style="text-align: justify; ">- утверждает форму бюллетеня для тайного голосования по выборам ректора (в бюллетени вносятся: название и дата конференции, фамилии, имена и отчества всех кандидатов в алфавитном порядке; в примечании к бюллетеню указывается, что голосование осуществляется путем вычеркивания или оставления в бюллетене соответствующих фамилий); </p>
 
<p style="text-align: justify; ">- готовит бюллетени для тайного голосования в количестве, равном числу выдвинутых на конференцию делегатов (бюллетени заверяются печатью и подписью председателя комиссии); </p>
 
<p style="text-align: justify; ">- организует проведение конференции по выборам ректора; </p>
 
<p style="text-align: justify; ">- оформляет протоколом список делегатов Конференции не позднее чем за 10 дней до даты проведения Конференции; </p>
 
<p style="text-align: justify; ">- организует регистрацию делегатов Конференции; </p>
 
<p style="text-align: justify; ">- организует выдачу бюллетеней для тайного голосования делегатам под расписку – функция счетной комиссии; </p>
 
<p style="text-align: justify; ">- направляет список кандидатов на должность ректора в Комиссию по аттестации; </p>
 
<p style="text-align: justify; ">- обеспечивает равные условия кандидатам на должность ректора; </p>
 
<p style="text-align: justify; ">- представляет итоги выборов в Министерство культуры Российской Федерации; </p>
 
<p style="text-align: justify; ">- осуществляет иные мероприятия, необходимые для проведения выборов ректора. </p>
 
<p style="text-align: justify; ">Заседания Комиссии проводятся по мере необходимости и считаются правомочными, если в них участвует не менее 2/3 членов ее списочного состава. Решения Комиссии принимаются большинством голосов от числа присутствующих на заседании ее членов. </p>
 
<p style="text-align: justify; ">Любые изменения состава Комиссии, в том числе довыборы ее членов осуществляются Ученым советом. </p>
 
<p> </p>
 
<p align="center"><b>4. Порядок выдвижения кандидатов на должность ректора </b></p>
 
<p style="text-align: justify; "><b> </b><b>4.1.</b> Выдвижение кандидатов на должность ректора Университета начинается с момента опубликования в СМИ объявления о выборах ректора Университета и заканчивается через 28 календарных дней в 18.00 часов по московскому времени. </p>
 
<p style="text-align: justify; ">Правом выдвижения кандидатов на должность ректора обладают учредитель, кафедры и другие структурные подразделения Университета. Возможно самовыдвижение кандидатов. Кафедры и другие структурные подразделения Университета также могут выразить поддержку тому или иному кандидату. </p>
 
<p style="text-align: justify; "><b> 4.2.</b> Каждая из перечисленных выше структур имеет право выдвинуть не более одного кандидата при его согласии. Выдвижение и поддержка от кафедр и других структурных подразделений оформляется протоколами заседаний (для принятия решения о выдвижении или поддержке кандидата в заседании подразделения участвует более половины его сотрудников, решение принимается не менее чем 50% голосов плюс один голос от числа присутствующих на заседании). Выдвинувшая кандидата структура передает в Комиссию выписку из протокола заседания за подписью председателя и секретаря, с указанием общего количества лиц, работающих в данном подразделении, количества лиц, присутствовавших на собрании, числа присутствовавших, принявших участие в голосовании и количества голосов, поданных в поддержку выдвинутого кандидата, против и воздержавшихся, лист регистрации. </p>
 
<p style="text-align: justify; ">Самовыдвижение осуществляется подачей письменного заявления от кандидата в Комиссию по подготовке и проведению выборов ректора. </p>
 
<p style="text-align: justify; ">Все документы подаются в Комиссию по подготовке и проведению выборов не позднее установленного настоящим Положением срока выдвижения кандидатов на должность ректора (п.4.1.). </p>
 
<p style="text-align: justify; "><b> 4.3.</b> Каждый кандидат (кроме случаев самовыдвижения) не позднее, чем за 30 рабочих дней до даты выборов подтверждает свое согласие на участие в выборах ректора письменным заявлением в Комиссию по подготовке и проведению выборов ректора. </p>
 
<p style="text-align: justify; "><b> 4.4.</b> Претенденты на должность ректора не позднее, чем за 30 рабочих дней до даты выборов представляют в Комиссию по подготовке и проведению выборов ректора следующие документы: </p>
 
<p style="text-align: justify; ">- заявление о намерении принять участие в выборах ректора; </p>
 
<p style="text-align: justify; ">- протоколы заседаний коллективов структурных подразделений Университета по выдвижению кандидатур к избранию на должность ректора, а также по поддержке кандидатур; </p>
 
<p style="text-align: justify; ">- автобиографию и список научных трудов кандидата; </p>
 
<p style="text-align: justify; ">- программу развития Университета; </p>
 
<p style="text-align: justify; ">- для претендентов, не работающих в Университете, заверенные копии документов о высшем образовании, ученой степени, ученом звании, почетном звании, выписку из трудовой книжки; </p>
 
<p style="text-align: justify; ">- дополнительные документы по усмотрению кандидата. </p>
 
<p style="text-align: justify; "><b> 4.5.</b> Комиссия по подготовке и проведению выборов ректора рассматривает в установленные сроки полученные документы и направляет их в Комиссию по аттестации не позднее, чем за 11 дней до даты очередного заседания Комиссии по аттестации. </p>
 
<p style="text-align: justify; "><b> 4.6.</b> В случае если все кандидаты на должность ректора прошли аттестацию в Комиссии по аттестации с отрицательным решением или не прошли аттестацию в установленные сроки, проведение Конференции по выборам ректора отменяется или переносится на срок не менее 3 месяцев. </p>
 
<p> </p>
 
<p align="center"><b>5. Порядок избрания делегатов на Конференцию </b></p>
 
<p style="text-align: justify; "><b> </b><b>5.1.</b> Комиссия по подготовке и проведению выборов ректора формирует списочный состав делегатов Конференции, исходя из того, что Ученый совет в полном составе входит в число делегатов Конференции и члены Ученого совета должны составлять не более 50 % общего числа делегатов. Другие члены коллектива Университета избираются на Конференцию в соответствии с квотами, утверждаемыми Ученым советом. </p>
 
<p style="text-align: justify; "><b> 5.2.</b> В состав делегатов Конференции входят (п. 71 Устава Университета): </p>
 
<p style="text-align: justify; ">- Ученый совет Университета (полностью); </p>
 
<p style="text-align: justify; ">- руководители структурных подразделений, не входящие в состав Ученого совета Университета, при численном составе подразделения свыше 10 человек (1:1); </p>
 
<p style="text-align: justify; ">- штатные преподаватели в соответствии с квотой (1:10). Если число штатных преподавателей превышает установленную квоту более чем на 50%, то число делегатов, избираемых на конференцию, увеличивается на одного участника; </p>
 
<p style="text-align: justify; ">- представители других категорий работников Университета в соответствии с квотой (1:20). По решению Комиссии выборы делегатов от структурных подразделений, численный состав которых не достигает установленной квоты, проводятся на общих (объединенных) собраниях при наличии кворума, определяемого в 2/3 общей численности штатных сотрудников. Если число сотрудников структурного подразделения (подразделений) превышает установленную квоту более чем на 50%, то число делегатов, избираемых на Конференцию, увеличивается на одного участника; </p>
 
<p style="text-align: justify; ">- представители обучающихся в соответствии с квотой (1 делегат от студентов очной формы обучения от каждого факультета, 1 делегат от аспирантов очной формы обучения). </p>
 
<p style="text-align: justify; "><b> 5.3.</b> Избрание делегатов от штатных преподавателей выпускающих кафедр происходит на собраниях соответствующих кафедр или на общих собраниях по факультетам, от штатных преподавателей общеуниверситетских кафедр – на совместном собрании соответствующих кафедр. По результатам собраний составляются протоколы. </p>
 
<p style="text-align: justify; ">Избрание делегатов от студентов очной формы обучения происходит на общих собраниях студентов по факультетам, по результатам которых составляются протоколы. </p>
 
<p style="text-align: justify; ">Избрание делегата от аспирантов дневной формы обучения происходит на общем собрании, по результатам которого составляется протокол. </p>
 
<p style="text-align: justify; "><b>5.4.</b> Решение об избрании делегатов принимается простым большинством голосов открытым голосованием при участии в голосовании не менее 50% от числа работников или обучающихся. Результаты голосования оформляются протоколом, который представляется в Комиссию. Общие собрания по избранию делегатов проводятся после определения Ученым советом даты конференции и заканчиваются за 15 календарных дней до ее проведения. </p>
 
<p style="text-align: justify; "><b>5.5. </b>Члены Ученого совета участвуют в собраниях по избранию делегатов на Конференцию в составе своих подразделений с правом решающего голоса. </p>
 
<p style="text-align: justify; ">В собраниях обучающихся по выдвижению и избранию делегатов на Конференцию участвуют студенты и аспиранты только дневной формы обучения. </p>
 
<p style="text-align: justify; "><b>5.6.</b> Заседания структурных подразделений, студентов и аспирантов считаются правомочными, если на них присутствует более половины от списочного состава. Избранными считаются выдвинутые кандидаты в делегаты, набравшие не менее чем 50% плюс один голос от числа присутствующих на собрании. </p>
 
<p style="text-align: justify; "><b> 5.7.</b> Протоколы и явочные листы заседаний с указанием фамилий избранных делегатов представляются в Комиссию по подготовке и проведению выборов ректора не позднее, чем за 15 дней до даты проведения выборов. </p>
 
