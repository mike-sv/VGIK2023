<p style="text-align: justify; "><b> 6.2.</b> Не позднее, чем за 5 дней до даты выборов ректора Комиссия по подготовке и проведению выборов вывешивает на доске объявлений список делегатов Конференции в произвольной форме и список лиц, аттестованных Комиссией по аттестации на должность ректора, по следующей форме:</p>
 
