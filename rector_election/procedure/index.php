<p style="text-align: justify; "><b>6.3. </b>Комиссия по подготовке и проведению выборов организует регистрацию делегатов Конференции.</p>
 
<p style="text-align: justify; "><b> 6.4</b>. Конференция считается правомочной, если в ее работе принимает участие не менее 2/3 от списочного состава делегатов. </p>
 
<p style="text-align: justify; "><b> 6.5.</b> Открывает и ведет Конференцию председатель Комиссии по подготовке и проведению выборов, который оглашает список кандидатов. Для ведения Конференции простым большинством голосов открытым голосованием избираются секретарь Конференции, мандатная по проверке полномочий делегатов (не менее 3 человек) и счетная (не менее 5 человек) комиссии. Председатели и секретари названных комиссий избираются этими комиссиями из числа их членов. Лица, баллотирующиеся на должность ректора, не могут входить в состав мандатной и счетной комиссий. </p>
 
<p style="text-align: justify; ">Председатель Комиссии по подготовке и проведению выборов предлагает регламент работы Конференции по выборам ректора, который утверждается делегатами Конференции открытым голосованием. </p>
 
<p style="text-align: justify; ">Решение мандатной комиссии по проверке полномочий делегатов оглашается ее председателем до начала тайного голосования и утверждается Конференцией простым большинством голосов открытым голосованием. </p>
 
<p style="text-align: justify; ">Председатель комиссии по подготовке и проведению выборов информирует делегатов о кандидатах на должность ректора, согласованных с Комиссией по аттестации. </p>
 
<p style="text-align: justify; ">Каждому из кандидатов предоставляется слово для изложения своей программы и ответов на вопросы делегатов Конференции по выборам ректора. Кандидаты на должность ректора выступают в алфавитном порядке. По решению Конференции может быть проведено обсуждение программ кандидатов на должность ректора. </p>
 
<p style="text-align: justify; "><b> 6.6.</b> Допускается снятие кандидатуры (самоотвод) на любом этапе выборов до момента объявления начала тайного голосования. </p>
 
<p style="text-align: justify; "><b> 6.7.</b> После выступления делегатов и других участников Конференции делегатам раздаются бюллетени для тайного голосования. Каждый делегат Конференции получает один бюллетень под расписку. Голосование за других лиц не допускается. Для проведения голосования в месте проведения Конференции по выборам ректора устанавливаются урны для бюллетеней. При этом должны быть созданы условия для осуществления тайного голосования. </p>
 
<p style="text-align: justify; ">Бюллетень считается действительным, если в нем оставлена незачеркнутой одна фамилия, в иных случаях бюллетень считается недействительным. </p>
 
<p style="text-align: justify; "><b> 6.8.</b> Подсчет голосов делегатов Конференции по выборам ректора начинается сразу после окончания тайного голосования и проводится без перерыва до установления итогов голосования. Перед началом подсчета голосов председатель счетной комиссии подсчитывает и погашает неиспользованные бюллетени, количество их вносится в протокол. Затем устанавливается число выданных бюллетеней. Эти данные заносятся в протокол. Члены счетной комиссии вскрывают урны с бюллетенями и производят подсчет голосов на основе избирательных бюллетеней в специально отведенном помещении. </p>
 
<p style="text-align: justify; "><b> 6.9</b>. После подсчета голосов делегатов Конференции по выборам ректора счетная комиссия составляет протокол об итогах голосования, в котором указываются следующие данные: </p>
 
<p style="text-align: justify; ">- общее число делегатов Конференции по списочному составу; </p>
 
<p style="text-align: justify; ">- число зарегистрированных делегатов; </p>
 
<p style="text-align: justify; ">- число выданных бюллетеней; </p>
 
<p style="text-align: justify; ">- число невыданных (неиспользованных) бюллетеней; </p>
 
<p style="text-align: justify; ">- число бюллетеней, оказавшихся в избирательных урнах; </p>
 
<p style="text-align: justify; ">- число действительных бюллетеней; </p>
 
<p style="text-align: justify; ">- число бюллетеней, признанных недействительными; </p>
 
<p style="text-align: justify; ">- число голосов, поданных за каждого кандидата, включенного в бюллетень. </p>
 
<p style="text-align: justify; ">Протокол подписывается всеми членами счетной комиссии. При этом любой член счетной комиссии может приложить к протоколу в письменном виде свое особое мнение. После оформления протокола счетная комиссия запечатывает все бюллетени в конверт, который подписывается членами счетной комиссии и скрепляется печатью Университета. Председатель счетной комиссии оглашает протокол с результатами тайного голосования. </p>
 
<p style="text-align: justify; ">Протокол утверждается делегатами Конференции открытым голосованием простым большинством голосов. </p>
 
<p> </p>
 
<p align="center"><b>7. Решение об итогах выборов ректора </b></p>
 
<p style="text-align: justify; "><b> </b><b>7.1</b>. По результатам тайного голосования Конференция принимает одно из следующих решений:<b> </b></p>
 
<p style="text-align: justify; ">- избрание одного из кандидатов на должность ректора Университета; </p>
 
<p style="text-align: justify; ">- назначение в случае необходимости второго тура; </p>
 
<p style="text-align: justify; ">- признание выборов несостоявшимися. </p>
 
<p style="text-align: justify; "><b> 7.2</b>. Выборы ректора считаются состоявшимися, если в Конференции принимает участие не менее 2/3 списочного состава делегатов Конференции. Избранным на должность ректора считается кандидат, получивший наибольшее число голосов, но не менее чем 50 % плюс один голос от количества действительных бюллетеней. Бюллетень считается недействительным, если в нем оставлено более одной из фамилий кандидатов на должность ректора, зачеркнуты все фамилии или отметки проставлены таким образом, что невозможно определить волеизъявление участника Конференции. </p>
 
<p style="text-align: justify; "><b> 7.3.</b> В случае если ни один кандидат не наберет необходимого количества голосов, на том же заседании Конференции по ее решению проводится второй тур голосования по выборам ректора Университета, в котором участвуют два кандидата, набравших наибольшее число голосов. Избранным в этом случае считается кандидат, за которого будет подано более 50% действительных бюллетеней. </p>
 
<p style="text-align: justify; "><b> 7.4.</b> Выборы признаются Конференцией несостоявшимися, если по результатам второго тура голосования делегатами Конференции ни за одного из кандидатов не было подано более 50% действительных бюллетеней. Процедура проведения новых выборов должна включать проведение заново всех вышеуказанных мероприятий. </p>
 
<p style="text-align: justify; "><b> 7.5.</b><b> </b>Ректор, избранный в соответствии с настоящим Положением, вступает в должность после утверждения его в должности Министерством культуры Российской Федерации и заключения с ним трудового договора.<b> 7.6.</b> Для утверждения в должности ректора Университета Комиссия по подготовке и проведению выборов представляет в Министерство культуры Российской Федерации соответствующие документы. Ответственность за подготовку всех документов для отправки в Министерство культуры Российской Федерации возлагается на Председателя Комиссии по подготовке и проведению выборов ректора. </p>
 
