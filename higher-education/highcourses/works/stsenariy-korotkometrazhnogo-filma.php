<p style="text-align: justify;">
	Учебный процесс: Курс состоит из лекций, практических занятий и разборов фильмов. Одно занятие – лекция, второе –&nbsp; разбор ваших работ. Курс начинается с описания героя. Позже идут заявки на истории, потом упражнения на диалоги и сами сценарии.&nbsp; Лекционный курс – живой материал, который постоянно развивается за счет примеров из фильмов, которые выходят в прокат, новых книг и т.д.&nbsp; В процессе лекций даю короткие сценарные задания.&nbsp; Но основная практическая работа – домашние задания на написание сценариев.&nbsp; Результат - написанный сценарий короткометражного фильма (или несколько сценариев – все зависит от вашей работоспособности).
