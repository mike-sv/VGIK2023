	 При рассмотрении апелляции обеспечивается соблюдение следующих требований в зависимости от категорий поступающих с ограниченными возможностями здоровья:
</p>
<p>
</p>
<ul>
	<li>для глухих и слабослышащих обеспечивается присутствие переводчика жестового языка;</li>
	<li>для слепых и слабовидящих обеспечивается присутствие тифлосурдопереводчика;</li>
	<li>для слепоглухих обеспечивается присутствие тифлосурдопереводчика.</li>
</ul>
<p>
</p>
<p>
	 5. После рассмотрения апелляции выносится решение апелляционной комиссии об изменении оценки результатов вступительного испытания или оставлении указанной оценки без изменения.
</p>
<p>
	 6. При возникновении разногласий в апелляционной комиссии проводится голосование, и решение принимается большинством голосов. При равенстве голосов решающим является голос председателя или его заместителя.
</p>
<p>
	 Оформленное протоколом решение апелляционной комиссии доводится до сведения поступающего (доверенного лица) и хранится в личном деле поступающего. Факт ознакомления поступающего (доверенного лица) с решением апелляционной комиссии заверяется подписью поступающего (доверенного лица).&nbsp;
</p>
<p>
</p>
<p>
	 16 сентября 2020 г. не позднее 15.00 по московскому времени в отдел аспирантуры и докторантуры ВГИК предоставляются для зачисления на места в рамках контрольных цифр оригинал диплома специалиста или магистра. Начало занятий для поступивших в аспирантуру в 2020 году: 21 сентября 2020 г.
</p>
<p align="center">
	 Информация об особенностях проведения вступительных испытаний&nbsp;для поступающих инвалидов
</p>
<p>
	 1. Граждане с ограниченными возможностями здоровья сдают вступительные испытания в порядке, установленном ВГИКом самостоятельно с учетом особенностей психофизического развития, индивидуальных возможностей и состояния здоровья (далее – индивидуальные особенности) таких поступающих.
</p>
<p>
</p>
<p>
	 2. При проведении вступительных испытаний обеспечивается соблюдение следующих требований: вступительные испытания проводятся в отдельной аудитории, количество поступающих в одной аудитории не должно превышать при сдаче вступительного испытания в устной форме 6 человек; допускается присутствие в аудитории во время сдачи вступительного испытания большего количества поступающих с ограниченными возможностями здоровья, а также проведение вступительных испытаний для лиц с ограниченными возможностями здоровья в одной аудитории совместно с поступающими, не имеющими ограниченных возможностей здоровья, если это не создает трудностей для поступающих при сдаче вступительного испытания; продолжительность вступительных испытаний по письменному заявлению поступающих, поданному до начала проведения вступительных испытаний, может быть увеличена по решению организации, но не более чем на 1,5 часа;
</p>
<p>
	 - присутствие ассистента (для инвалидов по слуху &#8210; переводчика жестового языка;
</p>
<p>
	 - для слепоглухих &#8210; тифлосурдопереводчика), оказывающего поступающим необходимую техническую помощь с учетом их индивидуальных особенностей (занять рабочее место, передвигаться, прочитать и оформить задание, общаться с экзаменатором);
</p>
<p>
</p>
<p>
	 - поступающим предоставляется в доступной для них форме инструкция по порядку проведения вступительных испытаний;
</p>
<p>
</p>
<p>
	 - поступающие с учетом их индивидуальных особенностей могут в процессе сдачи вступительного испытания пользоваться, необходимыми им техническими средствами;
</p>
<p>
</p>
<p>
	 - материально-технические условия должны обеспечивать возможность беспрепятственного доступа поступающих в аудитории, туалетные и другие помещения, а также их пребывания в указанных помещениях (наличие пандусов, поручней, расширенных дверных проемов, лифтов, при отсутствии лифтов аудитория должна располагаться на первом этаже; наличие специальных кресел и других приспособлений).
</p>
<p>
	 3. Дополнительно при проведении вступительных испытаний обеспечивается соблюдение следующих требований в зависимости от категорий поступающих с ограниченными возможностями здоровья:
</p>
<p>
	 а) для слепых:
</p>
<p>
	 - задания для выполнения на вступительном испытании, а также инструкция о порядке проведения вступительных испытаний зачитываются ассистентом;
</p>
<p>
	 - письменные задания надиктовываются ассистенту;
</p>
<p>
	 б) для слабовидящих:
</p>
<p>
	 - обеспечивается индивидуальное равномерное освещение не ниже 300 лк;
</p>
<p>
	 - поступающим для выполнения задания при необходимости предоставляется увеличивающее устройство.
</p>
 Особенности проведения приема иностранных граждан и лиц без гражданства
<p>
	 1. Иностранные граждане и лица без гражданства имеют право на получение высшего образования за счет бюджетных ассигнований в соответствии с международными договорами Российской Федерации, федеральными законами или установленной Правительством Российской Федерации квотой на образование иностранных граждан и лиц без гражданства (далее &#8210; квота на образование иностранных граждан), а также за счет средств физических лиц и юридических лиц в соответствии с договорами об оказании платных образовательных услуг (Часть 3 статьи 78 Федерального закона № 273-ФЗ).
</p>
<p>
</p>
<p>
	 2. Прием на обучение в пределах квоты на образование иностранных граждан осуществляется в соответствии с направлениями федерального органа исполнительной власти, осуществляющего функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования. Зачисление на обучение в пределах квоты на образование иностранных граждан осуществляется отдельным приказом (приказами) организации.
</p>
<p>
</p>
<p>
	 3. Иностранные граждане и лица без гражданства, являющиеся соотечественниками, проживающими за рубежом, имеют право на получение высшего образования наравне с гражданами Российской Федерации при условии соблюдения ими требований, предусмотренных статьей 17 Федерального закона от 24 мая 1999 г. № 99-ФЗ "О&nbsp;государственной политике Российской Федерации в отношении соотечественников за рубежом" (далее &#8210; Федеральный закон № 99-ФЗ).
</p>
<p>
</p>
<p>
	 4. При подаче документов, необходимых для поступления, иностранный гражданин или лицо без гражданства предоставляют следующие документы:
</p>
<p>
	 - копию паспорта иностранного гражданина либо иной документ, установленный федеральным законом или признаваемый в соответствии с международным договором Российской Федерации в качестве документа, удостоверяющего личность иностранного гражданина в соответствии со&nbsp;<a href="http://ivo.garant.ru/document?id=84755&sub=10">статьей&nbsp;10</a>&nbsp;Федерального закона от 25 июля 2002&nbsp;г. N&nbsp;115-ФЗ "О правовом положении иностранных граждан в Российской Федерации";
</p>
<p>
</p>
<p>
	 - оригинал документа об образовании и (или) квалификации (далее &#8210; документ об иностранном образовании и (или) иностранной квалификации) (или его заверенную в установленном порядке копию) в случае, если удостоверяемое указанным документом образование признается в Российской Федерации на уровне не ниже высшего образования (специалитет или магистратура) в соответствии с&nbsp;<a href="http://ivo.garant.ru/document?id=70191362&sub=109227">частями 1-3 статьи&nbsp;107</a>&nbsp;Федерального закона, а также в случае, предусмотренном законодательством Российской Федерации, оригинал свидетельства о признании документа об иностранном образовании и (или) иностранной квалификации, на уровне не ниже высшего образования (специалитет или магистратура) (или его заверенную в установленном&nbsp;<a href="http://ivo.garant.ru/document?id=10002426&sub=1300">порядке</a>&nbsp;копию);
</p>
<p>
</p>
<p>
	 - заверенный в установленном&nbsp;<a href="http://ivo.garant.ru/document?id=10002426&sub=81">порядке</a>&nbsp;перевод на русский язык документа об иностранном образовании и (или) иностранной квалификации и приложения к нему (если последнее предусмотрено законодательством государства, в котором выдан такой документ об образовании);
</p>
<p>
</p>
<p>
	 - фамилия, имя и отчество (при наличии) поступающего, указанные в переводах поданных документов, должны соответствовать фамилии, имени и отчеству (при наличии) поступающего, указанным во
</p>
<p>
	 въездной визе;
</p>
<p>
	 - 4 фотографии.
</p>
<p>
	 5. Иностранный гражданин или лицо без гражданства, являющиеся соотечественниками, проживающими за рубежом, представляют помимо документов, указанных в пункте 23 Порядка, оригиналы или копии документов, предусмотренных пунктом 6 статьи 17 Федерального закона № 99-ФЗ.
</p>
<p>
</p>
<p>
	 6. Иностранные граждане, которые поступают на обучение на основании международных договоров, представляют помимо документов, указанных в пункте 23 Порядка, документы, подтверждающие их отнесение к числу лиц, указанных в соответствующих международных договорах.
</p>
<p>
</p>
<p>
	 7. Прием иностранных граждан и лиц без гражданства на обучение по образовательным программам, содержащим сведения, составляющие государственную тайну, осуществляется только в пределах квоты на образование иностранных граждан с соблюдением требований, предусмотренных законодательством Российской Федерации о государственной тайне.
</p>
