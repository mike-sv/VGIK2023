<p align="center">
	 Критерии оценки по итогам вступительного испытания по иностранному языку
</p>
<p>
	 Основой для определения оценки на вступительном испытании служит уровень языковой компетентности поступающего, предусмотренный программой курса для студентов «Иностранный язык».
</p>
<p>
</p>
<p>
	 Оценка «отлично» предусматривает высокий уровень языковых умений и навыков поступающего в объеме программного материала, свободное владение всеми видами речевой деятельности, умение выполнять задания, связанные с речевой и письменной&nbsp; догадкой.
</p>
<p>
</p>
<p>
	 Оценки «хорошо» заслуживает поступающий, показавший знание программного материала, в объеме, необходимом для дальнейшей учебы, научной деятельности и предстоящей работы по профессии, успешно выполняющий предусмотренные экзаменационные задания. Как правило, оценка «хорошо» выставляется абитуриентам, показавшим хороший уровень освоения речевых навыков.
</p>
<p>
</p>
<p>
	 Оценка «удовлетворительно» выставляется абитуриентам, допустившим погрешности в ответе на экзамене.
</p>
<p>
</p>
<p>
	 Оценка «неудовлетворительно» выставляется поступающему, имеющему пробелы в знаниях основного программного материала, допустившему принципиальные ошибки в выполнении экзаменационных заданий и не продемонстрировавшему уровень коммуникативных умений.
</p>
