	 Правила подачи и рассмотрения апелляции
</p>
<p>
	 1. По результатам решения экзаменационной комиссии о прохождении вступительного испытания поступающий (доверенное лицо) вправе подать в апелляционную комиссию апелляцию о нарушении, по мнению поступающего, установленного порядка проведения вступительного испытания и (или) о несогласии с полученной оценкой результатов вступительного испытания.
</p>
<p>
</p>
<p>
	 2. Апелляция подается одним из способов:
</p>
<p>
</p>
<p>
</p>
<ul>
	<li>представляется поступающим или доверенным лицом;</li>
	<li>направляется во ВГИК через операторов почтовой связи общего пользования либо в электронной форме.</li>
</ul>
<p>
</p>
<p>
</p>
<p>
	 3. Апелляция подается в день объявления результатов вступительного испытания или в течение следующего рабочего дня.Рассмотрение апелляций проводится не позднее следующего рабочего дня после дня подачи апелляции.
</p>
<p>
</p>
<p>
	 4. При рассмотрении апелляции имеет право присутствовать поступающий (доверенное лицо), который должен иметь при себе документ, удостоверяющий его личность.
</p>
<p>
</p>
<p>
	 Рассмотрение апелляций не является пересдачей вступительного испытания. В ходе рассмотрения апелляции проверяется только соблюдение установленного порядка проведения вступительного испытания и (или) правильность оценивания результатов вступительного испытания.
</p>
