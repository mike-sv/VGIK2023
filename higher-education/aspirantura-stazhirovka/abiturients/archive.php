</p>
<p style="text-align: justify;">
	 2. Апелляция подается поступающим лично в день объявления результатов вступительного испытания или в течение следующего рабочего дня. При этом поступающий имеет право ознакомиться со своей работой выполненной в ходе вступительного испытания в порядке, установленной ВГИК. Приемная комиссия обеспечивает прием апелляций в течение всего рабочего дня. Рассмотрение апелляций проводится не позднее следующего рабочего дня после подачи апелляции.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3. Председателем приемной комиссии формируется апелляционная комиссия для рассмотрения апелляций во время работы приемной комиссии. Члены экзаменационной комиссии, чье решение оспаривается, в состав 13 апелляционной комиссии не включаются. В апелляционную комиссию рекомендуется включать в качества независимых экспертов деятелей культуры и искусств, не работающих в ВГИК.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4. При рассмотрении апелляции имеют право присутствовать члены экзаменационной комиссии, поступающий. Поступающий должен иметь при себе документ, удостоверяющий его личность.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 5. После рассмотрения апелляции выносится решение апелляционной комиссии о наличии или об отсутствии факта нарушения процедуры и о проведении или не проведении вступительного испытания повторно.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 6. При возникновении разногласий в апелляционной комиссии проводится голосование и решение утверждается большинством голосов. При равенстве голосов решающим является голос председателя или председательствующего на заседании апелляционной комиссии.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 7. Оформленное протоколом решение апелляционной комиссии доводится до сведения поступающего (под роспись) и хранится в личном деле поступающего.
</p>
<p style="text-align: justify;">
	 21 июля 2020 г. не позднее 15.00 по московскому времени в отдел аспирантуры и докторантуры ВГИК предоставляются для зачисления на места в рамках контрольных цифр оригинал диплома специалиста или магистра. Начало занятий для поступивших в аспирантуру в 2020 году: 01 сентября 2020 г.&nbsp;
</p>
<p align="center">
 <b>Информация об особенностях проведения вступительных испытаний </b><br>
 <b>
	для поступающих инвалидов</b>
</p>
<p align="center">
	 &nbsp;
</p>
<p style="text-align: justify;">
	 1. Граждане с ограниченными возможностями здоровья сдают вступительные испытания в порядке, установленном ВГИКом самостоятельно с учетом особенностей психофизического развития, индивидуальных возможностей и состояния здоровья (далее – индивидуальные особенности) таких поступающих.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2. При проведении вступительных испытаний обеспечивается соблюдение следующих требований:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; вступительные испытания проводятся в отдельной аудитории, количество поступающих в одной аудитории не должно превышать при сдаче вступительного испытания 6 человек;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; допускается присутствие в аудитории во время сдачи вступительного испытания большего количества поступающих с ограниченными возможностями здоровья, а также проведение вступительных испытаний для лиц с ограниченными возможностями здоровья в одной аудитории совместно с поступающими, не имеющими ограниченных возможностей здоровья, если это не создает трудностей для поступающих при сдаче вступительного испытания;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; продолжительность вступительных испытаний по письменному заявлению поступающих, поданному до начала проведения вступительных испытаний, может быть увеличена, но не более чем на 1,5 часа;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; присутствие ассистента (для инвалидов по слуху &#8210; переводчика жестового языка, для слепоглухих &#8210; тифлосурдопереводчика), оказывающего поступающим необходимую техническую помощь с учетом их индивидуальных особенностей (занять рабочее место, передвигаться, прочитать и оформить задание, общаться с экзаменатором);
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; поступающим предоставляется в печатном виде инструкция по порядку проведения вступительных испытаний;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; поступающие с учетом их индивидуальных особенностей могут в процессе сдачи вступительного испытания пользоваться необходимыми им техническими средствами;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; материально-технические условия должны обеспечивать возможность беспрепятственного доступа поступающих в аудитории, туалетные и другие помещения, а также их пребывания в указанных помещениях (наличие пандусов, поручней, расширенных дверных проемов, лифтов, при отсутствии лифтов аудитория должна располагаться на первом этаже; наличие специальных кресел и других приспособлений).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3. Дополнительно при проведении вступительных испытаний обеспечивается соблюдение следующих требований в зависимости от категорий поступающих с ограниченными возможностями здоровья:
</p>
<p style="text-align: justify;">
	 а) для слепых:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; задания для выполнения на вступительном испытании, а также инструкция о порядке проведения вступительных испытаний зачитываются ассистентом;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; письменные задания надиктовываются ассистенту;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 б) для слабовидящих:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; обеспечивается индивидуальное равномерное освещение не ниже 300 лк;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; поступающим для выполнения задания при необходимости предоставляется увеличивающее устройство;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; задания для выполнения, а также инструкция о порядке проведения вступительных испытаний оформляются увеличенным шрифтом, возможно также использование собственных увеличивающих устройств;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; задания для выполнения, а также инструкция по порядку проведения вступительных испытаний оформляются увеличенным шрифтом.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 в) для глухих и слабослышащих обеспечивается наличие звукоусиливающей аппаратуры коллективного пользования, при необходимости поступающим предоставляется звукоусиливающая аппаратура индивидуального пользования;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 г) необходимости поступающим предоставляется звукоусиливающая аппаратура индивидуального пользования;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 д) для слепоглухих предоставляются услуги тифлосурдопереводчика (помимо требований выполняемых соответственно для слепых и глухих);
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 е) для лиц с тяжелыми нарушениями речи, глухих, слабослышащих все вступительные испытания по желанию поступающих могут проводиться в письменной форме;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 ж) для лиц с нарушениями опорно-двигательного аппарата (тяжелыми нарушениями двигательных функций верхних конечностей или отсутствием верхних конечностей):
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; письменные задания выполняются на компьютере со специализированным программным обеспечением или надиктовываются ассистенту;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &#8210; по желанию поступающих все вступительные испытания могут проводиться в устной форме.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4. Условия, указанные в пунктах 2 и 3 Правил, предоставляются поступающим на основании заявления о приеме, содержащего сведения о необходимости создания соответствующих специальных условий.
</p>
 <b>
<p style="text-align: center;">
	 Особенности проведения приема иностранных граждан и лиц без гражданства
</p>
 </b>
<p>
	 &nbsp;
</p>
<p style="text-align: justify;">
	 1. Иностранные граждане и лица без гражданства имеют право на получение высшего образования за счет бюджетных ассигнований в соответствии с международными договорами Российской Федерации, федеральными законами или установленной Правительством Российской Федерации <a href="http://ivo.garant.ru/document?id=70368236&sub=1">квотой</a>
	на образование иностранных граждан и лиц без гражданства (далее - квота на образование иностранных граждан), а также за счет средств физических лиц и юридических лиц в соответствии с договорами об оказании платных образовательных услуг.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2. Прием и зачисление иностранных граждан и лиц без гражданства в пределах установленной Правительством Российской Федерации квоты на образование иностранных граждан и лиц без гражданства осуществляется в соответствии с <a href="http://ivo.garant.ru/document?id=70664076&sub=1000">Порядком</a>
	отбора иностранных граждан и лиц без гражданства в пределах установленной Правительством Российской Федерации квоты на образование иностранных граждан и лиц без гражданства в Российской Федерации, утвержденным <a href="http://ivo.garant.ru/document?id=70664076&sub=0">приказом</a>
	Министерства образования и науки Российской Федерации от 28 июля 2014&nbsp;г. N&nbsp;844 (зарегистрирован Министерством юстиции Российской Федерации 3 октября 2014&nbsp;г., регистрационный N&nbsp;34236).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3. Зачисление иностранных граждан и лиц без гражданства в пределах <a href="http://ivo.garant.ru/document?id=70368236&sub=1">квоты</a>
	на образование иностранных граждан и лиц без гражданства осуществляется по направлениям, выданным Министерством образования и науки Российской Федерации
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4. Иностранные граждане и лица без гражданства, являющиеся соотечественниками, проживающими за рубежом, имеют право на получение высшего образования наравне с гражданами Российской Федерации при условии соблюдения ими требований, предусмотренных <a href="http://ivo.garant.ru/document?id=12015694&sub=17">статьей&nbsp;17</a>
	Федерального закона от 24 мая 1999&nbsp;г. N&nbsp;99-ФЗ "О государственной политике Российской Федерации в отношении соотечественников за рубежом"
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 5. Соотечественники, проживающие за рубежом, являющиеся участниками <a href="http://ivo.garant.ru/document?id=89653&sub=1000">Государственной программы</a> по оказанию содействия добровольному переселению в Российскую Федерацию соотечественников, проживающих за рубежом, утвержденной <a href="http://ivo.garant.ru/document?id=89653&sub=0">Указом</a>
	Президента Российской Федерации от 22 июня 2006&nbsp;г. N&nbsp;637 "О мерах по оказанию содействия добровольному переселению в Российскую Федерацию соотечественников, проживающих за рубежом" (далее - Государственная программа), и члены их семей имеют право на получение высшего образования по программам ассистентуры-стажировки в соответствии с Государственной программой.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 6. Прием и зачисление иностранных граждан вассистентуру-стажировку ВГИКа&nbsp; на обучение на основе договоров об оказании платных образовательных услуг осуществляется в соответствии с правилами приема ВГИКа, в том числе на основании результатов дополнительных вступительных испытаний творческой направленности.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 7. Прием документов иностранных граждан, поступающих на обучение на основе договоров об оказании платных образовательных услуг, осуществляется в сроки, определяемые Институтом.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 8. Иностранные граждане, поступающие на обучение на основе договоров об оказании платных образовательных услуг, при подаче заявления (на русском языке) о приеме в образовательную организацию предоставляют следующие документы:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 - копию паспорта иностранного гражданина либо иной документ, установленный федеральным законом или признаваемый в соответствии с международным договором Российской Федерации в качестве документа, удостоверяющего личность иностранного гражданина в соответствии со <a href="http://ivo.garant.ru/document?id=84755&sub=10">статьей&nbsp;10</a>
	Федерального закона от 25 июля 2002&nbsp;г. N&nbsp;115-ФЗ "О правовом положении иностранных граждан в Российской Федерации";
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 - оригинал документа об образовании и (или) квалификации (далее &#8210; документ об иностранном образовании и (или) иностранной квалификации) (или его заверенную в установленном порядке копию) в случае, если удостоверяемое указанным документом образование признается в Российской Федерации на уровне не ниже высшего образования (специалитет или магистратура) в соответствии с <a href="http://ivo.garant.ru/document?id=70191362&sub=109227">частями 1-3 статьи&nbsp;107</a> Федерального закона, а также в случае, предусмотренном законодательством Российской Федерации, оригинал свидетельства о признании документа об иностранном образовании и (или) иностранной квалификации, на уровне не ниже высшего образования (специалитет или магистратура) (или его заверенную в установленном <a href="http://ivo.garant.ru/document?id=10002426&sub=1300">порядке</a>
	копию);
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 - заверенный в установленном <a href="http://ivo.garant.ru/document?id=10002426&sub=81">порядке</a>
	перевод на русский язык документа об иностранном образовании и (или) иностранной квалификации и приложения к нему (если последнее предусмотрено законодательством государства, в котором выдан такой документ об образовании);
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 - фамилия, имя и отчество (при наличии) поступающего, указанные в переводах поданных документов, должны соответствовать фамилии, имени и отчеству (при наличии) поступающего, указанным во въездной визе;
</p>
<p>
	 - 4 фотографии.
