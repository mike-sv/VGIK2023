 <b>Правила подачи и рассмотрения апелляции</b>
</p>
<p style="text-align: justify;">
	 По результатам вступительного испытания поступающий имеет право подать в апелляционную комиссию письменное апелляционное заявление о нарушении, по его мнению, установленного порядка проведения и (или) несогласии с его результатом (далее – апелляция).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1. Рассмотрение апелляции не является пересдачей вступительного испытания. В ходе рассмотрения апелляции проверяется только соблюдение установленного порядка проведения вступительного испытания.
</p>
