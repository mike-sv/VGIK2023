 <b>Перечень вступительных испытаний и их приоритетность при ранжировании списков поступающих</b>
</p>
<p>
</p>
<p style="text-align: justify;">
	 Поступающие сдают следующие вступительные испытания:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1) специальную дисциплину, вступительное испытание, соответствующее профилю программы ассистентуры-стажировки по творческо-исполнительской специальности (далее &#8210; профильное вступительное испытание) по программе для поступающих. Профильное вступительное испытание предшествует вступительным испытаниям по философии и иностранному языку и проводится в два этапа: представление творческой программы (проекта) и собеседование (коллоквиум);
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2) иностранный язык, определяемый институтом и необходимый ассистенту-стажеру для выполнения научно-квалификационной работы (диссертации) на соискание ученой степени кандидата наук.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Примечание: подпунктами «1), 2)» указана приоритетность вступительных испытаний.
