?><h2>Ректор ВГИК</h2>
<p>
 <img width="199" alt="В.С.Малышев.jpg" src="/upload/medialibrary/963/v.s.malyshev.jpg" height="300" hspace="3" vspace="3" align="right" apgn="right" style="float:left; margin-right:10px;">
