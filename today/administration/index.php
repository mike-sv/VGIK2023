<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b><span itemprop="rucovodstvo">Малышев Владимир Сергеевич </span></b><span itemprop="rucovodstvo">-</span><b><span itemprop="rucovodstvo">&nbsp;</span></b><span itemprop="post">ректор</span> Всероссийского государственного университета кинематографии имени С. А. Герасимова, Президент Ассоциации учебных&nbsp; заведений искусства и культуры. Кандидат экономических наук, доктор искусствоведения, профессор, заслуженный работник культуры Российской Федерации,&nbsp; академик Российской Академии образования.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p>
</p>
<p style="text-align: justify;">
	 Родился 11 ноября 1949 г. в селе Новопетровское Истринского района Московской области. Окончил экономический факультет Всесоюзного государственного института кинематографии (ВГИК) в 1975 г. В 1987 году окончил Высшую партийную школу (ВПШ) при ЦК КПСС. В 1990 году после избрания коллективом был&nbsp; назначен на должность генерального директора Госфильмофонда СССР (ныне Госфильмофонд России). В этот период Госфильмофонд получает статус особо&nbsp; ценного объекта культурного наследия, становится одним из ведущих киноархивов мира. При этом в трудные годы экономического кризиса его усилиями&nbsp; Госфильмофонд обретает новое 10-этажное здание фильмохранилища, Фестивальный культурный центр, жилой дом для сотрудников. С сентября 2001 г.&nbsp; назначен заместителем министра культуры Российской Федерации, курировал вопросы экономической деятельности. В 2004–2007 годах – заместитель&nbsp; руководителя Федерального агентства по культуре и кинематографии.
</p>
<p>
</p>
<div style="text-align: justify;">
	 В 2007 году после избрания на должность ректора возглавил Всероссийский государственный институт кинематографии имени С. А. Герасимова.
</div>
<div style="text-align: justify;">
	 Член Правления и секретарь Союза кинематографистов России по вопросам профессионального кинообразования. Член Российской Академии&nbsp; кинематографических искусств "Золотой Орел". Член Союза писателей, Президент "Ассоциации вузов культуры и искусств России". Доверенное лицо Президента&nbsp; Российской Федерации В.В. Путина.
</div>
 <br>
<div style="text-align: justify;">
	 Награждён медалями «В память 850-летия Москвы», «В память 300-летия Санкт-Петербурга», «В память 1150-летия Великого Новгорода», орденами Дружбы (1999), «Петра Великого I степени» (2004), «За заслуги перед Отечеством» IV (2005), III (2009) степеней, Святого благоверного князя Даниила Московского (2003), Святителя Макария Митрополита Московского III степени (2006), Святого Иннокентия III степени (2009), Казахским орденом Дружбы, орденами Франции искусств и словесности (2000) и за заслуги в искусстве и литературе (2013). Вручен памятный знак "Самарский Крест" в честь празднования 135 годовщины Освобождения Болгарии от османского ига Чрезвычайным и Полномочным Послом Республики Болгария в РФ (2013).
</div>
<p style="text-align: justify;">
	 Лауреат Государственной премии Российской Федерации в области литературы и искусства за вклад в сохранение и популяризацию культурного наследия, развитие традиций и модернизацию отечественного кинематографического образования (2010); премии Правительства РФ в области культуры (2008). Является членом коллегии Министерства культуры РФ, членом Совета по кино при Правительстве РФ, членом Совета при Президенте РФ по культуре и искусству, членом Совета по общественному телевидению России, действительный член Евразийской Академии Телевидения и Радио. Указом Президента РФ в 2002 году присвоен квалификационный разряд действительного государственного советника Российской Федерации 2 класса.<br>
 <br>
	 В. С. Малышев является автором 15 киносценариев и многочисленных публикаций по вопросам кино, а также продюсером. Он состоит в Международной федерации киноархивов (ФИАФ) и Европейской ассоциации киноархивов, а также является действительным членом Российской Академии кинематографических искусств и наук и Киноакадемии «Ника», Евразийской академии телевидения.
</p>
<p style="text-align: justify;">
	 В. С. Малышев более 30 лет работал в сфере культуры и кинематографии, был одним из организаторов и в течение нескольких лет заместителем директора Всесоюзного объединения "Союзкинорынок".&nbsp;<span style="text-align: justify;">Автор сценариев и продюсер документальных фильмов, среди которых «Кому живётся весело, вольготно на Руси».&nbsp;</span>Он является автором и первым президентом Кинофестиваля архивного кино «Белые Столбы» и кинофестиваля «Балтийские дебюты».
</p>
