          <br />
         </p>
       
        <p align="justify">The film directing students were given 2-3 days to discuss their ideas with the masters and teachers of the school. The shootings took 4-5 days, and the editing process lasted 4 days. As for acting students, they had to perform exercises in speech, dance, stage movement, etc. under the supervision of VGIK leading professors.</p>
       
        <p> 
          <br />
         </p>
       
