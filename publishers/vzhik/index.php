<p align="justify">      &laquo;ВЖИК&raquo; - это аббревиатура, которая складывается из четырех слов - ВГИК, Жизнь, Искусство, Кино. Эти четыре слова характеризуют студенческое издание и служат неисчерпаемым источником вдохновения. 
