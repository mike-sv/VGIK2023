<ul>
	<li>1. The Publication Requirements<br>
 </li>
</ul>
<p>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 The articles submitted to the editorial office are subject to the double blind peer-reviewing (see <b>Rules of Peer-reviewing</b>). The reviews are kept in the editorial office and the publishing office within 5 years.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Before the publication the author has to sign a license agreement at the VGIK Publishing Office (tel. 8 (499)181-35-07; e-mail: <a href="mailto:rio@vgik.info"><span style="color: #9d0a0f;">rio@vgik.info</span></a>).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul>
	<li>2. The articles must contain:<br>
 </li>
</ul>
<p>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 a) The title of the article, printed in bold type. The title has to be succinct.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 b) The author’s full name, academic degree and academic rank (if any) or specify "postgraduate student".&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 c) An abstract: 5 – 7 lines. (in bold type).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 d) The keywords (5 – 6 words in bold type).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 e) The text of the article (no more than 15 – 20 000 of printed characters with spaces, 1,5-spaced font Times New Roman, 14 pt; footnotes should be indicated in the end of each page; numbered notes should be indicated in the end of the article).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 f) In exceptional cases the volume of the article can be expanded to 40 000 characters.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 g) The references are listed in the end of the text. (After the word "References" in bold, a colon is put and a list of the publications is provided in alphabetical order).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul>
	<li>3. Attachment to the article:&nbsp;<br>
 </li>
</ul>
<p>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 a) Illustrations and photographs (4 to 6) in the electronic form are to be sent as separate JPEG files (resolution: 300dpi or higher, the size of each file: 1,5 MB or above).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 b) Diagrams and figures with the following indication "Table 1,2..", " Fig.1,2..".
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 c) The author's photo in the electronic form (JPEG,&nbsp;resolution: no less than 300dpi, size: no less than 600 KB, color or black-and-white).&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul>
	<li>4. <b>Recommendations for the &nbsp;Authors</b><br>
 </li>
</ul>
<p>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 The articles written by <b>postgraduates and PhDs</b> are accompanied by an extract from the minutes of the chair or another academic body meeting recommending the article for publication (duly certified and sealed) and, for post-graduates, a reference from the post-graduate department.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Authors with <b>Doctoral degrees</b> need no extract from the minutes of the chair or another academic body.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul>
	<li>5. The reviews and the minutes of department meetings have to be delivered to the<b> VGIK Research Department</b>:<br>
 </li>
</ul>
<p>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 a) The articles of postgraduate students and degree-seekers are not considered the extract from the minutes of the meeting&nbsp; of the chair or other academic body. This extract may be &nbsp;scanned and sent together with the article, the information about the author, photos and illustrations. &nbsp;But the printed version must be presented to the&nbsp;editional board&nbsp;either personally or by mail.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 It should contain&nbsp; the information about the discussion of the article, recommendation for its publication with a concise justification of its academic novelty.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 b) <b>After the presentation of the extract from the minutes of the meeting # ___ of the chair or other academic body</b> to the VGIK Research Department, the testing of the article for its compliance with the journal’s technical requirements, the receiving of the acknowledgement of receipt stating the date the article was accepted and its code to <a href="mailto:vestnik-vgik@vgik.info"><span style="color: #9d0a0f;">vestnik-vgik@vgik.info</span></a><span style="color: #9d0a0f;"> (</span><a href="mailto:vestnik-vgik@inbox.ru"><span style="color: #9d0a0f;">vestnik-vgik@inbox.ru</span></a>) the electronic version of the article is sent to the journal’s editorial board under a cover letter mentioning that the review and extract from the minutes of the meeting&nbsp; of the chair or other academic body were submitted&nbsp;(in exceptional cases it is possible to follow point 4.a) stating the article’s code and the date of acceptance mentioned in the acknowledgement of receipt.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 c) In view of encouraging the research activity of VGIK students the chairs and other academic divisions can recommend student papers for publication in «Vestnik VGIK». These papers must be supplemented with an extract from the minutes of the chair or another academic body meeting recommending the article for publication (duly certified and sealed) and, for post-graduates, a reference from the post-graduate department. VGIK student papers are reviewed on the usual terms.
</p>
