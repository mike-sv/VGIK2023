		<p style="text-align: justify;">
			 1. Присланные в редакцию статьи проходят <b>процедуру двойного “слепого”&nbsp;</b><span style="text-align: justify;"><b>рецензирования</b></span><span style="text-align: justify;">&nbsp;(см.&nbsp;</span><b style="text-align: justify;"><u>Правила рецензирования</u></b><span style="text-align: justify;">),&nbsp;</span>рецензии сохраняются в редакции и издательстве (РИО ВГИК) в течение 5-ти лет. Перед публикацией в научном журнале «Вестник ВГИК» автору&nbsp;<span style="text-align: justify;">необходимо подписать</span><span style="text-align: justify;">&nbsp;</span><span style="text-align: justify; color: #9d0a0f;"><span style="color: #9d0a0f;"><span style="color: #9d0a0f;"><span style="color: #9d0a0f;"><span style="color: #9d0a0f;"><a href="/upload/science/licenzdogovor2021.doc"><span style="color: #9d0a0f;">лицензионный договор</span></a></span></span></span></span></span><span style="text-align: justify;">&nbsp;</span>в РИО ВГИК (тел.8 (499)181-35-07; e-mail: <a href="mailto:rio@vgik.info">rio@vgik.info</a>).&nbsp;
		</p>
		<p>
			 2.&nbsp;<b>Статьи принимаются оформленными следующим образом:</b>
		</p>
		<p style="text-align: justify;">
			 а) Заголовок статьи выделяется полужирным шрифтом. Желательно, чтобы
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 заголовок был емким, раскрывающим тему статьи и по возможности кратким.
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 б) ФИО, ученая степень и ученое звание (если имеется) или укажите «аспирант».
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 в) Аннотация: 5 – 7 строк. (Слово «<b>Аннотация</b>:» пишется полужирным шрифтом, после него ставится двоеточие, далее приводится текст).
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 г) Ключевые слова (5 – 6 слов) — (Слова «<b>Ключевые слова</b>:» пишутся полужирным шрифтом).
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 д) Текст статьи (не более 15—20 тыс. знаков с пробелами, интервал — 1,5; шрифт Times New Roman, кегль 14; сноски постраничные; примечания в конце статьи — пронумерованные).
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 В исключительных случаях и по согласованию объем статьи может быть расширен до 40 000 знаков.
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 е) После окончания текста статьи приводится список литературы (Обозначается словом «<b>Литература</b>:» полужирным шрифтом, далее ставится двоеточие и приводится перечень изданий в алфавитном порядке).
		</p>
		<p style="text-align: justify;">
		</p>
		<p>
			 3.<b> К статье прилагаются</b>:
		</p>
		<p style="text-align: justify;">
			 а) иллюстрации или фотоснимки в электронном виде высылаются отдельными файлами (формат JPG, разрешение не менее 300 dpi, размер каждого фото файла — от 1,5 МГб и выше, количество — от 4 до 6).
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 б) Качественно выполненные схемы и рисунки с указанием — Таблица 1,2.., Рис.1,2..,
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 в) Фотография автора (портрет) в электронном виде (формат JPG, разрешением не менее 300 dpi, «вес» — не менее 600 Кб, можно ч/б).&nbsp;
		</p>
		<p>
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 4. Отдельным файлом высылаются &nbsp;<b>Cведения об авторе</b>, в которых повторяются <b>аннотация</b> и ключевые слова к статье:
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 а) <b>Название статьи</b> (полужирным шрифтом);
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 б) <b>УДК</b> (шифр библиотечной Универсальной Десятичной Классификации, определяемый согласно утвержденным на международном уровне таблицам УДК). &nbsp;Этот шифр уточняется в библиографическом отделе библиотеки ВГИК
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 либо любой другой библиотеки;
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 в) <b>Сведения об авторе </b>-&nbsp;до 5-ти строк (ФИО, ученая степень, ученое звание (если есть), должность, место работы, место учебы в аспирантуре);
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 г)<b> Аннотация </b>к статье («<b>Аннотация</b>:»&nbsp; - пишется с двоеточием полужирным шрифтом);
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 д) <b>Ключевые </b>слова («<b>Ключевые слова</b>:» - пишется с двоеточием&nbsp;<span style="text-align: justify;">полужирным шрифтом).</span><span style="text-align: justify;">&nbsp;</span>
		</p>
		<p>
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 5.<b> В редакцию журнала</b> сдаются рецензии и протокол заседания кафедры или другого научного подразделения:
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
 <b>а)</b> Без соответствующих рецензий и протокола статьи аспирантов и соискателей ученой степени кандидата наук не рассматриваются. Возможно, отсканировав рецензии и протокол, выслать их по электронной почте вместе со статьей, сведениями об авторе, фотографией и иллюстрациями. Однако обязательным является бумажная версия рецензий и протокола, которая приносится в редакцию журнала или посылается почтовым отправлением.
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
 <b>б) Требования к оформлению</b> рецензии см. на сайте.
		</p>
		<p style="text-align: justify;">
 <b>в) Рецензентами статьи аспиранта или соискателя ученой степени могут&nbsp; быть как кандидаты, так и доктора наук соответствующей специализации</b>. Внутренняя рецензия пишется: педагогом кафедры или научным сотрудников учреждения, где учится аспирант или соискатель; внешняя рецензия готовится представителем сторонней организации. Выписка из протокола заседания кафедры или другого научного подразделения по месту учебы аспиранта или соискателя должна содержать информацию об обсуждении данной статьи, рекомендацию к её публикации и краткое обоснование тематической новизны работы.
		</p>
		<p style="text-align: justify;">
 <b>г) После предоставления двух положительных рецензий</b> и выписки из протокола №__ заседания кафедры или другого научного подразделения в отдел научного развития ВГИК, проверки статьи на техническое соответствие требований к публикации, получения расписки о сдаче статьи в редакцию журнала с указанием даты приема и шифра статьи на электронный адрес&nbsp;<a href="mailto:vestnik-vgik@vgik.info">vestnik-vgik@vgik.info</a> (<a href="mailto:vestnik-vgik@inbox.ru">vestnik-vgik@inbox.ru</a>) направляется электронная версия статьи с сопроводительным письмом, где обязательно упоминается, что рецензии и выписка из протокола сданы (в исключительных случаях можно поступить как указано в пункте 4.а) с указанием шифра статьи и даты сдачи, которые указаны в расписке.
		</p>
		<p style="text-align: justify;">
 <b>д) </b>В целях стимулирования научно-исследовательской деятельности студентов ВГИК кафедры и другие научные подразделения могут рекомендовать студенческие работы к публикации в «Вестнике ВГИК». Для данных публикаций необходимо наличие выписки из решения заседания ведущей кафедры или другого научного подразделения о рекомендации данной статьи к публикации в «Вестнике ВГИК» и две рецензии – внутренняя ( одного из педагогов кафедры, мастера курса, сотрудника научного подразделения) и внешняя (по рекомендации Отдела научного развития ВГИК). Статьи студентов ВГИКа проходят рецензирование в редакции на общих основаниях.&nbsp;
		</p>
		<p>
			 6. <b>Рекомендации по оформлению списка литературы и сносок</b>:
		</p>
		<p style="text-align: justify;">
			 Пример (для сносок – сноски в статье постраничные):
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 1. Тарасова В.И. Политическая история Латинской Америки: учеб. для вузов — 2-е зд. — М.: Проспект, 2006. С.305.
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 2. Адорно Т.В. К логике социальных наук // Вопросы философии.1992.-№ 10. С.76 – 86.
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 3. Философия культуры и философия науки: проблемы и гипотезы: межвуз. сб. науч. тр./ Сарат. гос. ун-т; [под ред. С.Ф. Мартыновича]. Саратов: Изд-во Сарат. ун-та, 1999. — 199 с.
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 Если текст цитируется не по первоисточнику, а по другому документу, то в начале ссылки приводят слова: «Цит. по:» (цитируется по), «Приводится по:», с указанием источника заимствования.
		</p>
		<p>
 <b>Пример</b>:
		</p>
		<p>
			 Цит. по: Соловьёв В.С. Красота в природе: соч. в 2 т. – М.: Прогресс, 1988. Т. 1.&nbsp; С. 35-36.
		</p>
		<p>
			 Повторную ссылку на один и тот же документ или его часть приводят в сокращенной форме при условии, что все необходимые для идентификации и поиска этого документа библиографические сведения указаны в первичной ссылке на него. Выбранный приём сокращения библиографических сведений используется единообразно для данного документа.&nbsp;
		</p>
		<p>
 <b>Пример</b>: Соловьёв В.С. Указ. соч. Т. 1. С. 37 или Соловьёв В.С. Красота в природе. М.; 1988. Т. 2. С. 27
		</p>
		<p style="text-align: justify;">
			 7. Оформление по ГОСТу Списка литературы и сносок, взятых из электронных&nbsp;источников. <b>Обращаем внимание</b>!: после электронного адреса в круглых скобках приводят сведения о дате обращения к электронному сетевому ресурсу; после слов «дата обращения» указывают число, месяц и год.
		</p>
		<p>
 <b>Пример</b>:
		</p>
		<p style="text-align: justify;">
			 1. Весь Богородский уезд: форум// Богородск-Ногинск. Богородское краеведение// URL: <a href="http://www.bogorodsk-noginsk.ru/forum/">http://www.bogorodsk-noginsk.ru/forum/</a> (дата обращения 20.02.2007).
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 2 Инвестиции останутся сырьевыми // PROGNOSIS.RU// URL: http//<a href="http://www.prognosis.ru/print.html?id=6464">www.prognosis.ru/print.html?id=6464</a> (дата обращения 20.02.2007).
		</p>
		<p style="text-align: justify;">
		</p>
		<p style="text-align: justify;">
			 3 Члиянц Г. Создание телевидения // QRZ.RU// URL: <a href="http://www.qrz.ru/articles/article260.html">http://www.qrz.ru/articles/article260.html</a> (дата обращения 21.02.2006).
		</p>
		<p>
			 8. Список литературы сопровождается <b>транслитерацией</b> русского алфавита латиницей (см. ссылку:<a href="http://gost.ruscable.ru/cgi-bin/catalog/catalog.cgi?i=6464">http://gost.ruscable.ru/cgi-bin/catalog/catalog.cgi?i=6464</a>)
		</p>
		<p>
 <b>Пример</b>:
		</p>
