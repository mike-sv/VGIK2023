<p style="text-align: justify;">
 <b>1. Acceptance Rules</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1.1. Authors present original independent works not published elsewhere earlier, marked with actuality and academic novelty, along with a set of documents (in paper and electronic versions) to be tested for their compliance with the journal’s technical requirements.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1.2. After the format of the article is checked and the set of the required documents is completed the author gets an acknowledgement of receipt stating the date the article was accepted and its code and on these grounds the article is to be sent to the journal’s editorial board.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1.3. The articles are to be sent to <a href="mailto:vestnik-vgik@vgik.info"><span style="color: #9d0a0f;">vestnik-vgik@vgik.info</span></a> accompanied by of a personal request for publication addressed to the editor-in-chief of "Vestnik VGIK" Urazova Svetlana Leonidovna.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>2. The articles are supplemented with:</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1. The author's photo and illustrations (5 - 6 at most, resolution: 300 dpi) with captions. The authors guarantee that the iconographic material doesn't infringe the copyright and other intellectual property rights.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.2. The so-called "Information about the Author" (a Word file) in Russian stating in one indented paragraph the title of the article, the UDC, the author’s full name, degree and scholarly rank (if any), position and place of employment, as well as an abstract of the article (5-7 lines at most) and 5-6 key words. In the process of compiling the journal the "Information about the Author" is shaped as an independent document being part of "Summary/Presentation of Authors" and sent for translation.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.3. An extended abstract of the article in Russian and English no longer than 2200 characters with spaces (for every chapter), containing (in one indented paragraph) the title of the article, the UDC, the author’s full name, degree and scholarly rank (if any), the summary of the article (a new paragraph) which reveals its message and academic novelty in a concise and well-argued way, and the main conclusions and key words.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>3. The basic requirements to the articles</b>: The article is to be a completed text upon a subject of actual importance and academic novelty, original and thoroughly researched and never published elsewhere before.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4. The articles written by <b>postgraduates and PhDs</b> are accompanied by an extract from the minutes of the chair or another academic body meeting recommending the article for publication (duly certified and sealed) and, for post-graduates, a reference from the post-graduate department.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Authors with Doctoral degrees need no extract from the minutes of the chair or another academic body.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 5. The articles submitted to the editorial office are subject to the double blind peer-reviewing (see <b>Rules of Peer-reviewing</b>). The reviews are kept in the editorial office and the publishing office within 5 years.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 6. <b>Recommendations for the Authors</b>:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 6.1. The recommended volume of the article is 15-20 thousand characters with spaces, 8–12 typed pages. The text’s parts must be headlined.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 6.2. The typescript,(after the title and the author’s full name) is provided with the thematic index according to the tables of the Universal Decimal Classification (UDC). The recommendation is available in VGIK’s library. Then follows a short <b>abstract</b> (5-7 lines) and 5 or 6 <b>key words</b>. The typescript is accompanied by supplements: the <b>Information about the Author</b> in Russian and <b>an extended abstract</b> in Russian and English (2200 characters each) in separate files.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 6.3.&nbsp;<b>The text.</b> The article is a completed work of 15–20 thousand characters with spaces in the Word format, with a heading, an abstract, key words and the text set in type in 14 Times New Roman body-size with the 1,5 interline, to be sent. Outside authors should also send their articles on paper by mail to the editorial board with a return receipt. All the parts of the article (tables, diagrams, drawings, footnotes etc.) are placed and shaped according to the National State Standard (GOST) and technical requirements and also sent separately in JPEG. When citing the tables, drawings etc., their sources are to be mentioned by all means.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 7.&nbsp;<b>The footnotes</b> in the article are presented per page. The list of bibliographic references entitled REFERENCES is placed in the end, 10 titles at most. They are<b> transliterated into Roman alphabet</b>, see: <a href="http://gost.ruscable.ru/cgi-bin/catalog.cgi?i=6464"><span style="color: #9d0a0f;">http://gost.ruscable.ru/cgi-bin/catalog.cgi?i=6464</span></a>.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 The example of transliteration see below.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
