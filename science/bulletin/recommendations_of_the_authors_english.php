<p style="text-align: justify;">
	 8.1.&nbsp;<b>Abbreviations and conventional signs</b>. Those not accepted by the International System of Units are to be deciphered in the text or in the footnotes.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 8.2.&nbsp;<b>Illustrations</b>. The article is supplemented with photos, drawings, diagrams etc. in 300 dpi sent to the editorial office separately in JPEG with captions in separate files. Conventional signs are explained by captions or in the text. Lines, dots and names must be absolutely clear and distinct. If other authors’ illustrations are used one should observe the copyright and the intellectual property status as required by the RF Civil Code (at greater length see <a href="http://www.consultant.ru"><span style="color: #9d0a0f;">www.consultant.ru</span></a>) and obtain the author’s written permission for publication.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 8.3. A separate file contains the <b>Information about the Author</b>: the title of the article, UDC, the author’s full name, academic degree and rank (if any), place of employment, position, home address, contact telephone number and e-mail.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 8.4. After the procedure of double blind peer-reviewing (if both reviews are positive) and the approval of the publication by the Editorial Board the author is notified be e-mail. In the case of disapproval the author gets a motivated denial.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 8.5. The Editorial Board retains the right – by agreement with the author – to correct the title and to carry out editing and correcting. After the publication the Editorial Board has the right to place the article on its site or other sites with reference to "Vestnik VGIK".
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
