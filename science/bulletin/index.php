		<p align="justify">
			 Научный информационно-аналитический журнал «Вестник ВГИК» является ведущим научным периодическим изданием Всероссийского государственного университета кинематографии имени С.А. Герасимова. Решением Президиума Высшей Аттестационной комиссии Минобрнауки России «Вестник ВГИК» включен в Перечень ведущих рецензируемых научных журналов и изданий, в которых должны быть опубликованы основные научные результаты диссертаций на соискание ученых степеней доктора и кандидата наук, отвечающие требованиям ВАК по научным направлениям: «Искусствоведение», «Философские науки», «Экономические науки». <br>
			 В журнале печатаются работы научных и педагогических работников, докторантов, аспирантов и студентов. В «Вестнике ВГИК» также публикуется информация о научной жизни факультетов ВГИК, о конференциях и дискуссиях, проходящих в институте, о книгах, учебниках и учебных пособиях, авторами которых выступают сотрудники ВГИК. Журнал рассчитан на научных работников, преподавателей высшей и средней школы, аспирантов, студентов, а также для широкого круга читателей, интересующихся достижениями российской науки в области экранных искусств.
