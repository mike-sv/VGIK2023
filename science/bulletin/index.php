		<p align="justify">
			 The academic journal “Vestnik VGIK” ("Bulletin of Film Art”) is the leading scientific periodical issued by the Russian State University&nbsp;of Cinematography named after Sergei Gerasimov. This peer-reviewed journal is on the list of scientific periodicals and editions approved by the Supreme Attestation Commission (VAK) Presidium of the RF Ministry of Education and Science for publishing the major scientific results of dissertations for the advanced academic degrees of Dsc and PhD meeting the requirements of the Supreme Attestation Commission in the fields of Art History, Philosophy, Economics.</p><p align="justify">
			 The journal publishes original research papers by scientists and educational workers, doctoral, post-graduate and undergraduate students, as well as information on VGIK faculties research activity, conferences and discussions, books, textbooks and teaching aids compiled by VGIK professors. Its target audience comprises scholars, university professors, school teachers, post-graduates, undergraduates and others interested in the developments of Russian cinema studies.
