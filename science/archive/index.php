</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 С 1999 г. в рамках Международного фестиваля ВГИК проводятся конкурсы на лучшие работы по истории и теории кино, конкурс на лучшую исследовательскую работу в области экономики кинопроизводства. Авторитетное жюри определяет победителей и присуждает дипломы. В 1995 г. премии "Св. Анна" на Открытом Российском конкурсе творческих работ кинематографических вузов была удостоена работа студента Д. Островского "Борис Барнет и мутации культурного сознания конца 20-х годов".
