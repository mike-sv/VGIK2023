<p style="text-align: justify;">
	 Аспиранты института ежегодно участвуют в конкурсах на соискание премий и грантов для молодых ученых. Так, В 2001 году был выделен грант на проект аспирантки кафедры эстетики, истории и теории культуры Чукреевой Н.А."Исследования эволюции изображения и роли экранных изображений в процессе формирования видения мира современного человека".
</p>
<p style="text-align: justify;">
