<p style="text-align: justify; "><b>2 ноября 2011 года</b> состоялось третье в этом сезоне заседание Дискуссионного клуба «Парадоксы современной художественной культуры», проведённого кафедрой эстетики, истории и теории культуры совместно с отделом научного развития. Для обсуждения была выбрана тема «<b>Эстетика в эпоху неклассических форм художественной культуры. Парадоксы эволюции</b>». Вела дискуссию доктор философских наук, профессор Н.Б. Маньковская. Специальным гостем заседания дискуссионного клуба был профессор Ольденбургского университета Михаэль Зук&aacute;ле (Германия).</p>
 
<p style="text-align: justify; "> На обсуждении присутствовало 50 человек, в том числе и гости из Института философии РАН. Тема сообщения Михаэля Зукале была обозначена как «Эстетика между наукой и искусством: современное состояние и перспективы». Зукале рассказал о кризисе эстетики в Германии в послевоенный период, который, в отличие от кризиса в философии, так и не был преодолён. Корень кризиса Зукале видит в специфике основного предмета эстетики – единого и целостного воздействия объекта искусства. Это воздействие с большим трудом поддаётся анализу, поскольку анализ всегда предполагает приписывание предмету определённых признаков (предикатов), в результате чего предмет «разлагается» и теряет свою целостность. Для понимания сегодняшней социокультурной ситуации Зукале считает чрезвычайно важным изучать историю искусств, поскольку в ней можно найти примеры самых неожиданных преломлений научных открытий в творчестве художников. </p>
 
