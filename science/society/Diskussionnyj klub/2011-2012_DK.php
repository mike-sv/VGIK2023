<div style="text-align: justify; "><b>7 сентября 2011 года</b> состоялось первое в новом учебном году заседание дискуссионного клуба «Парадоксы современной художественной культуры». Для обсуждения была предложена тема: «<b>Кинематограф и русское зарубежье: проблемы сохранения культурной традиции</b>». В рамках клуба, в актовом зале университета, был показан фильм «Анастасия», режиссер В.П. Лисакович. Фильм назван по имени главной героини Анастасии Александровны Ширинской-Манштейн - свидетельницы исхода русского флота из Севастополя в 1920 году в Бизерт (Тунис). В фильме ярко, искренно и честно рассказывается об истории жизни русских моряков в Бизерте, о сохранении традиций русской культуры.</div>
 
<p></p>
 
<p style="text-align: justify; ">Затем состоялся доклад киноведа, доктора искусствоведения Н.И. Нусиновой, который был посвящен деятельности кинематографистов-эмигрантов и сопровождался показом найденных и восстановленных фильмов этого периода. Последующая дискуссия была посвящена обсуждению личности героини фильма, миссии русской эмиграции, пласту русской эмиграционной культуры, которую мы только начинаем открывать.</p>
 
