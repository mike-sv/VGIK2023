  <p><b>28 марта 2012 года</b> состоялось <b>пятое заседание</b> дискуссионного клуба <b>&laquo;Парадоксы современной художественной культуры&raquo;</b>. Основой обсуждения был выбран показанный ранее фильм А.Н. Сокурова «Фауст», созданный по литературному сценарию Ю.Н. Арабова.</p>
 
  <p>Вел дискуссию доктор искусствоведения, профессор кафедры эстетики, истории и теории культуры Н.А. Хренов, рассмотревший социокультурные трансформации, происходившие с сюжетом «Фауста», от «вечного образа» к «культурному символу» эпохи ХХ века – «сверхчеловеку», имеющему право на неограниченное самоосуществление. Выступления присутствующих были посвящены анализу различных смысловых пластов, заложенных в фильме – литературного, изобразительного, музыкально-шумового и др. При разности высказываний и оценок все участники дискуссии сошлись на мнении, что драматург и режиссер проводят завораживающее исследование генеалогии европейского индивидуализма не как «абстрактно-философской» проблемы, а как биографической, вещественной в своей конкретике исторической драмы. </p>
 
  <p>Заканчивая дискуссию, участвующие отметили, что в обществе назрела необходимость в глобальных произведениях, претендующих на высказывание о состоянии мира и «Фауст» А.Н. Сокурова и Ю.Н. Арабова – яркая, талантливая работа, которая затрагивает множество философских вопросов и заставляет задуматься. </p>
