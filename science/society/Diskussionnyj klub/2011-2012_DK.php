<p style="text-align: justify; "><u>Заседания дискуссионного клуба 2011-2012 </u></p>
 
<p><u> </u></p>
 
<p><u> </u></p>
 
<p style="text-align: justify; "> 
  <p><b>20 апреля 2012 года</b> состоялось <b>шестое заседание</b> дискуссионного клуба. Темой обсуждения стала эволюция книги, а точнее ее интерактивная электронная форма. Присутствующие рассуждали о том, что такое интерактивная электронная книга &ndash; аттракцион или закономерность эволюции. Вела дискуссию доктор искусствоведения, профессор кафедры режиссуры анимационного фильма Н.Г. Кривуля, которая представила приглашенных гостей клуба – О.Г. Яцюк, доктора искусствоведения, профессора, заведующую кафедрой информационных технологий в дизайне Национального института дизайна и К. Коломацкую – выпускницу Национального института дизайна, дипломной работой которой стала интерактивная книга русских сказок. </p>
 
  <p>В обсуждении проблемы отмечалось, что форма книги не является постоянной характеристикой. Она определяется развитием общества и культуры. Электронная книга позволяет создать объемное пространство книжного текста, состоящее из системы связанных между собой и подчиняющихся основному тексту субтекстов, в которых читатель (благодаря информационным технологиям) получает свободу перемещения в любом направлении. Нелинейность, принципиальная открытость, расширяющая поле смыслов книги, объективность и толерантность к читателю – вот часть изменений печатной книги в ее новой материально-конструктивной форме. Эти изменения соответствуют требованиям, социальному заказу информационного общества и информационных технологий как способа деятельности в нем. </p>
 
  <p>Дальнейшая дискуссия была посвящена технологическим аспектам создания электронной книги и вызвала особенно большой интерес у студентов факультета анимации и мультимедиа.</p>

