<p style="text-align: justify; "><b>10 октября 2011 года</b> состоялось второе в этом сезоне заседание дискуссионного клуба «Парадоксы современной художественной культуры». Для обсуждения была выбрана тема: «<b>Спорные вопросы российской истории в современном кинематографе</b>». Вел дискуссию - зав. кафедрой истории и философии профессор А.Л. Андреев. В дискуссии принимали участие гости клуба – известный философ О.В. Аронсон из Института философии РАН, Людмила Васильевна Сурова (бакалавр богословия, член Союза писателей России, руководитель литературной студии «Чистые пруды»). А.Л. Андреев открыл дискуссию сообщением об отражении истории Российского государства в современном кинематографе. В ходе дискуссии анализировались фильмы «Царь» П.Лунгина, «1612» В.Хотиненко, «Раскол» Н.Досталя и др. Присутствующие отмечали исторические несоответствия, «смакование жестокости», «размывание исторического сознания» в фильмах современного кинематографа, тогда как «истина рождается в столкновении фактов, глубоком изучении исторического материала». </p>
 
