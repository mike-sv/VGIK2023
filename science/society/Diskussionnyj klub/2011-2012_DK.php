<p style="text-align: justify; "><b>27 февраля 2012 года</b> в зале заседаний Учёного совета ВГИК состоялось заседание Дискуссионного клуба «Парадоксы современной художественной культуры». Заседание, тема которого была обозначена как «<b>Искусство между Сциллой и Харибдой</b>», было посвящено выходу книги «Триалог. Живая эстетика и философия искусства» авторов В.В. Бычкова, Н.Б. Маньковской и В.В. Иванова. На заседании присутствовали авторы книги, руководители издательства «Прогресс-Традиция», в котором вышла книга, сотрудники Московской государственной юридической академии, Института философии РАН, Финансового университета при Правительстве Российской Федерации, представители профессорско-преподавательского состава ВГИКа, сотрудники лабораторий, отдела научного развития, аспиранты и студенты. </p>
 
<div> 
  <p style="text-align: justify; ">Участники были единодушны в том, что «Триалог» можно смело назвать энциклопедией современной художественной культуры, написанной в живой и увлекательной манере. Отмечалась, с одной стороны, укоренённость «Триалога» в традиции «диалогического философствования», с другой, его острая актуальность как нового средства преодоления монологической традиции отечественной философской и культурной рефлексии, за которой скрывается нетерпимость к иным взглядам. Затрагивалась тема переворота в представлениях о гуманизме и о человеке, произошедшая в ХХ в. Участники воздали должное смелости авторов, которые вопреки тенденции к узкой специализации обращаются к всеобщим, универсальным проблемам культуры, чем способствуют привлечению широкого круга читателей к серьёзной гуманитарной литературе. </p>
 
