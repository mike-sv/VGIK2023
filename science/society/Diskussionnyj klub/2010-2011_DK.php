<p style="text-align: justify; ">29 сентября было проведено первое в 2010/2011 учебном году заседание Дискуссионного клуба на тему «<b>Критика – </b><b>теория искусства – эстетика: точки пересечения</b>». С программным докладом выступила профессор И.П. Никитина, которая в своей докторской диссертации, посвященной эстетическим аспектам художественного пространства, и дальнейших исследованиях, продолжает изучать вопросы соотношения эстетики и искусствоведения. И.П. Никитина подчеркнула, что и у эстетики и у искусствоведения существует единый предмет исследования – искусство как таковое. Однако есть и определенные различия: искусствоведение, по сравнению с эстетикой, имеет более описательный характер, анализирует конкретные произведения искусства, стилевые направления, допуская неограниченное количество оценок и норм, тогда как эстетика размышляет об общих вопросах искусства, является философией искусства. </p>
 
<p style="text-align: justify; ">По мнению И.П. Никитиной, художественная критика ошибочно отнесена к наукам, поскольку допускает антинаучную субъективную оценочность. Д.А. Салынский и Н.А. Дымшиц в своих выступлениях возразили И.П. Никитиной, считая художественную критику наукой, поскольку она обладает всеми признаками науки, главным из которых признается наличие системы логических доказательств. Выступление Н.Е. Мариевской было посвящено определению критериев оценки и обоснованию аргументированности позиций, по которым то или иное явление можно относить к науке или к околонаучной деятельности.  </p>
 
