<p style="text-align: justify; ">22 октября состоялось второе в 2010/2011 учебном году заседание Дискуссионного клуба на тему «<b>Отечественная </b><b>философия в контексте культуры: парадоксы развития советской эпохи</b>». Гостем заседания стала известный ученый, доктор философских наук, зав. отделом историко-философских исследований Института философии РАН Н.В. Мотрошилова. </p>
 
<p style="text-align: justify; ">В своем выступлении Н. В. Мотрошилова затронула проблемы поиска взаимосвязей между развитием философской мысли в России и за рубежом в 1950-1980-е гг. и охарактеризовала трудности и противоречия работы советских философов в те годы. Н. В. Мотрошилова отметила антиномичность развития советской философии, трудности, с которыми сталкивались философы в своей работе из-за сильнейшего идеологического контроля, пронизывавшего все сферы деятельности. Дискуссия, развернувшаяся после выступления Н. В. Мотрошиловой, была посвящена поиску ответов на вопросы – почему послевоенное время сегодня так привлекательно для создателей произведений искусства, как и возможно ли отразить в кинематографе философские размышления об эпохе. </p>
 
