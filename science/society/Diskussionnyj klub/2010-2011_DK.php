  <p style="text-align: justify; ">2 марта 2011г. состоялось очередное заседание дискуссионного клуба &laquo;Парадоксы современной художественной культуры&raquo;. Для обсуждения была предложена тема «<b>Парадоксы </b><b>новой жизни «старого» кино</b>». Гостем клуба, в этот день, стал кандидат искусствоведения, заведующий отделом истории отечественного кино НИИ киноискусства Н.А. Изволов, занимающийся компьютерной реставрацией старых кинолент. </p>
 
  <p style="text-align: justify; ">В своем выступлении Н.А. Изволов сравнил реставрацию старых кинолент с палеонтологией, т.к. по фрагменту воссоздается целое, но насколько точна эта реконструкция, никто не знает, потому что оригинал не известен. </p>
 
  <p style="text-align: justify; ">Многие фильмы утеряны безвозвратно и те немногие фрагменты, которые дошли до нашего времени, являются ценным материалом для изучения истории кино, языка кино, изучения кино как явления культуры того времени и характеристики самого времени. </p>
 
  <p style="text-align: justify; ">Метод, представленный автором, стал возможен с появлением компьютерной техники. В основе этого метода лежит реконструкция идеи автора с помощью оригинальных материалов, найденных исследователем. Собирая и накапливая материалы по истории кино (фотографии, срезки пленок, чертежи декораций, монтажные листы, списки титров и т.п.). Классифицируя их, готовя эти материалы к экспонированию, сканируя их, стало понятно, что всю эту информацию можно собрать в монтажной программе. Н.А. Изволов подчеркивает, что его работа это &ndash; академическое исследование, имеющая основанием реальные факты, выстроенные в определенную концепцию. Присутствовавшие на заседании преподаватели университета высказали пожелание об издании учебного пособия для студентов ВГИК, посвящённого методике реставрации архивных фильмов. </p>
 
