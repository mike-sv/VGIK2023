		 Основная цель клуба – научный анализ противоречивых тенденций современной отечественной и мировой культуры, а также различные искусствоведческие, эстетические, культурологические и философские интерпретации этих процессов. Тематика докладов, выступлений и обсуждений касается теоретической и критической рефлексии текущих процессов художественной жизни, трансформаций культурных традиций, изменений роли искусства в общественной жизни, взаимодействия искусства с научно-техническим прогрессом и коммерческими институциями. На заседаниях Дискуссионного клуба выступают ведущие преподаватели ВГИКа, а также приглашенные извест¬ные ученые, деятели культуры, мастера искусства из России и зарубежных стран. Всемерно поощряется участие в обсуждениях аспирантов и студентов, которые тем самым пополняют свои знания, расширяют кругозор и осваивают навыки публичного анализа явлений художественной культуры.<br>
