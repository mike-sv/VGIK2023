		 В октябре 2009 года во ВГИКе начал работу Дискуссионный клуб&nbsp;<b>«Парадоксы современной художественной культуры»</b>. Инициатором этой акции стала кафедра эстетики, истории и теории культуры, которая осуществляет разработку тематического плана и основную организационную работу по подготовке заседаний.
