<p>
  <div style="text-align: justify; ">27 апреля в 16-30 в конференц-зале ВГИКа прошло шестое заседание Дискуссионного клуба, посвященное теме «<b>Кино между мифом и реальностью</b>». Гостями заседания клуба стали: доктор </div>

  <div style="text-align: justify; ">филологических наук, профессор Института русского языка и литературы им. </div>

  <div style="text-align: justify; ">А.С. Пушкина С.М. Телегин; кандидат филологических наук, доцент </div>

  <div style="text-align: justify; ">Московского государственного педагогического университета</div>
</p>
 
<p style="text-align: justify; ">Е. Ю. Полтавец; кандидат филологических наук, доцент Московского областного университета Т. А. Алпатова. </p>
 
<p style="text-align: justify; ">К дискуссии были предложены следующие вопросы: каковы границы между мифом и реальностью; в чём различия между киноправдой и киномифом; из чего складываются мифологемы российского кино. </p>
 
<p style="text-align: justify; ">С. М. Телегин в своем докладе коснулся общих вопросов, связанных с возникновением мифа, принципами его существования в искусстве. </p>
 
<div> 
  <p style="text-align: justify; ">Ю. Н. Арабов рассказал об онтологии мифа в драматическом сюжете, отметив, что миф и мифология свойство человеческого сознания. На конкретных примерах зав. кафедры драматургии кино пояснил, что в российском христианском мифе возможны три сюжетные ситуации: жертва, предательство и воскрешение. </p>
 
  <p style="text-align: justify; ">Л. А. Зайцева в своем выступлении, основываясь на известном исследовании известного российского философа А. Ф. Лосева «Диалектика мифа» проанализировала фильмы И. Пырьева, М. Донского, С. Герасимова и на конкретных примерах объяснила, как развивается мифология в советском кино, как в кинематографе на смену религиозному мифу приходит мифологема, основанная на коммунистической идеологии. </p>
 
  <p style="text-align: justify; ">Дальнейшие выступления касались исследования мифологии, лежащей в основе отдельных кинокартин. Горячо обсуждались вопросы, связанные с необходимостью различения метафоры и мифа в искусстве, организации сакрального пространства в драматическом сюжете, типологии мифов от социокультурного до религиозного, определении границ мифологического в творчестве. </p>
 
