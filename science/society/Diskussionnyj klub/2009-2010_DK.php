<p style="text-align: justify; "><u>Заседания дискуссионного клуба 2009-2010 </u></p>
 
<p> </p>
 
<p> </p>
 
<p style="text-align: justify; ">3 июня 2010 года состоялось седьмое заседание Дискуссионного клуба на тему: &laquo;<b>Кино и религия</b>&raquo;. Перед началом заседания в был показан фильм «Поп» В. Хотиненко, получивший Гран-При на Международном кинофоруме «Золотой витязь» </p>
 
<p style="text-align: justify; ">Начал заседание Дискуссионного клуба профессор Н.А. Хренов, который отметил, что в последние годы религиозная тема в российском кино возрождается. Об этом свидетельствуют фильмы «Остров» П. Лунгина, «Юрьев день» К. Серебренникова, «Чудо» А. Прошкина и «Поп» В. Хотиненко, имевшие успех у зрителей. В.А. Утилов отметил трудности, которые всегда встречаются на пути кинематографистов, пытающихся отразить духовные темы в кинолентах. Также он подчеркнул характерные особенности зарубежного трансцендентного кинематографа. </p>
 
<p style="text-align: justify; ">Гость Дискуссионного клуба, профессор А.П. Назаретян, высказался о том, что насаждение религиозной проблематики средствами искусства может порождать в постсоветском обществе негативную реакцию, поэтому в целях формирования религиозного сознания необходимо развивать трансцендентный кинематограф, говорящий о духовных вопросах не напрямую, а с помощью использования образного языка кино. В качестве положительных примеров такого кинематографа он привел фильмы Л. Шепитько, А. Звягинцева. </p>
 
<p style="text-align: justify; ">Профессор кафедры драматургии кино и ТВ Л.Н. Нехорошев обратил внимание собравшихся на многообразие фильмов, к которым подходит определение «религиозно-христистианский» и предложил свою классификацию этих фильмов. Завершил дискуссию В.И. Мильдон, обративший внимание на то, что любое настоящее искусство всегда шире той или иной идеологии, религиозной в частности. </p>
 
