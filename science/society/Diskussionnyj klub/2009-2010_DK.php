<p style="text-align: justify; ">27 декабря 2009 г. состоялось третье заседание дискуссионного клуба «Парадоксы современной художественной культуры». Для обсуждения была предложена следующая тема: «<b>Границы современного искусства</b>». </p>
 
<div> 
  <p style="text-align: justify; "> Заседание клуба открыл председатель А. В. Новиков. Затем Н.Б. Маньковская зачитала короткое сообщение, вводящее в проблематику данного вопроса. Были озвучены две основополагающие точки зрения на проблему проницаемости границ современно искусства: первая заключается в утверждении отсутствии явных границ между художественным произведением и бытовым миром, проницаемости и взаимозаменяемости художественных объектов и предметов повседневности. Вторая позиция утверждает необходимость конкретного качественного отличия произведения искусства от иных материальных форм. </p>
 
  <p> </p>
 </div>
 
