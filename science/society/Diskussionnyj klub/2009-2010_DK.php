<p style="text-align: justify; ">Второе засе­дание дискуссионного клуба «Парадоксы современной художе­ственной культуры», которое прошло в ноябре 2009 г., было посвящено теме «<b>Гоголь: парадоксы интерпретации</b>». Поводом стал 200-летний юбилей великого писателя. Тайна творчества, жизни и смерти Гоголя, новые интерпретации его книг и судьбы стали предметом дискуссии. Участникам были представлены – фильм доцента кафедры режиссуры Д. Демина «Гоголь: портрет загадочного гения» и отчет о выставке в Риме действительного члена Российской академии худо­жеств, народного художника России, профессора ВГИКа С.А. Алимова. В центре обсуждения были две культурологиче­ские проблемы: что такое художественная мысль и как соот­носится творчество писателя с его биографией, т.е. с обыден­ной жизнью. Литературоведческому анализу творчества Гоголя был посвящён доклад профессора В.И. Мильдона. </p>
 
<p style="text-align: justify; ">Стенограмма дискуссии опубликована в научно-аналитическом журнале «Вестник ВГИК», 2010, №3-4. </p>
 
