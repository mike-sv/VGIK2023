<p style="text-align: justify; ">27 февраля 2010 г. состоялось четвёртое заседание Дискуссионного клуба «Парадоксы современной художественной культуры». Для обсуждения была предложена следующая тема: «<b>Чехов &ndash; это наше всё?</b>». В рамках Дискуссионного клуба прошла встреча с театральным режиссёром Камой Гинкасом. Заседание клуба открыл доклад Л.М. Ельницкой, посвящённый современным литературоведческим трактовкам творчества А.П. Чехова. Были затронуты проблемы классического прочтения его произведений, отмечены многие спорные положения консервативных интерпретаций, обозначены проблемы кинематографических экранизаций чеховских произведений. </p>
 
<p style="text-align: justify; ">Следующий доклад В.И. Мильдона был посвящён сравнительному анализу творческого пути А.П. Чехова и Н.В. Гоголя. Докладчиком было отмечено некоторое сходство двух биографий, а также проведены определённые параллели между художественным методом двух писателей. </p>
 
<p style="text-align: justify; ">Выступление Камы Гинкаса было посвящено проблемам постановки чеховских произведений на театральной сцене. Также в процессе обсуждения были подняты вопросы значения времени в произведениях Чехова, проблема авторского отношения к героям произведений, особенности поэтики абсурда в произведениях Чехова. </p>
 
