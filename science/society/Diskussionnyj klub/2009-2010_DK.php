<p style="text-align: justify; ">14 октября 2009 г. состоялось первое заседание дискуссионного клуба «Парадоксы современной художественной культуры». Для обсуждения была предложена следующая тема: «<b>Постмодернизм и псевдопостмодернизм в России</b>». </p>
 
<p style="text-align: justify; ">Заседание клуба открыл председатель А.В. Новиков. Затем Н.Б. Маньковская совершила введение в проблематику предмета. Она перечислила и выделила характерные признаки влиятельного сегодня течения в искусстве, дала справку об истории его возникновения на Западе и в России. </p>
 
<p style="text-align: justify; ">Основную часть заседания занял доклад преподавателя русской литературы П.Е.Спиваковского на тему «Модернизм и постмодернизм». Выступление было посвящено рассмотрению творчества известного современного писателя Владимира Сорокина, относимого современной критикой к постмодернизму. Автор предложил непривычный взгляд, во многом оспаривающий общераспространенные представления. В исходном тезисе П.Е.Спиваковский позиционировал Сорокина как художника, не вписывающегося в контекст постмодернизма, но скорее, наоборот, укорененного в традиции, а именно, использующего трагические черты, стремящегося к целостности, оперирующего серьезными смыслами. </p>
 
