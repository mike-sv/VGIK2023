<p style="text-align: justify; ">24 марта состоялось пятое заседание Дискуссионного клуба «Парадоксы современной художественной культуры». Для обсуждения была предложена следующая тема: «<b>Наука и искусство на телевидении и в кино: культурные парадоксы</b>». </p>
 
<p style="text-align: justify; ">Помимо педагогов кафедры эстетики, истории и теории культуры, на заседании присутствовали доктор биологических наук О. Лопина, режиссёры И. Васильева, Н. Полонская, В. Ман, Н. Лацис. </p>
 
<p style="text-align: justify; "> Заседание клуба открыло выступление режиссёра И. Васильева, посвящённое общим проблемам производства, кинопроката и телепоказа научно-популярных и просветительских фильмов. Было озвучено предложение о необходимости создания государственного телеканала, посвящённого научно-популярному кино и учебным фильмам для поднятия уровня научной эрудиции населения. </p>
 
<p style="text-align: justify; ">В ходе дальнейшей дискуссии особенно активно обсуждались проблемы доминирования на отечественном телевизионном экране псевдонаучных и квазидокументальных картин. Основные причины этого, как посчитало большинство выступавших, заключаются в сильном падении общего образовательного уровня, а также в подмене научно-популярных фильмов фильмами-проповедями, весьма вольно обобщающих и интерпретирующих научные данные. </p>
 
