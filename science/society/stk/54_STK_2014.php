?>30 марта 2014 года состоялась 68-ая встреча Студенческого творческого клуба (СТК) в рамках Студенческого научно-творческого общества (СНТО). Тема встречи: &laquo;Метаморфозы желаемого&raquo;. В контексте диспута состоялся просмотр фильма режиссера Андрея Богатырева «Иуда» (2013) и его обсуждение с режиссером картины. 
<br />
 
<br />
 Студенты обсуждали парадоксальность христианского учения и образа Иуды в трактовке произведения Андреева; ценности, цели и различия персонажей, формы их существования и желаний. Особое внимание было уделено технической стороне кинофильма, проблемным местам касающимся драматургии, съемочного процесса. 
<br />
 
<br />
 На встрече присутствовало 25 человек. 
<br />
 
<br />
 В дискуссии принимали участие студенты разных факультетов ВГИКа, в том числе и аспиранты. 
