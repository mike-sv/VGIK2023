<ul>
	<li><b style="text-align: justify;"><span style="font-size: 11pt;">С 19 мая по 5 июня 2022 года в Москве пройдёт Всероссийская декада выпускников творческих вузов.&nbsp;</span></b></li>
	<li><b><span style="font-size: 11pt;">Организаторы мероприятия: Министерство культуры Российской Федерации, Ассоциация учебных заведений искусства и культуры и ФГБУК РОСКОНЦЕРТ.</span></b></li>
	<li><b><span style="font-size: 11pt;">30 мая в&nbsp;19:00, в Концертном зале Чайковского, состоится центральное событие проекта – гала-концерт участников Всероссийской декады выпускников творческих вузов.</span></b></li>
