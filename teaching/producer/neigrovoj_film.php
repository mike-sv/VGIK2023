<h3>
<ul>
	<li>Образовательные фильмы<br>
 </li>
</ul>
 </h3>
<p style="text-align: justify;">
	 Ещё одна категория фильмов, которую по распространенной и уже укоренившейся ошибке относят к документальному кино&nbsp;— это образовательные (учебные) фильмы, предназначенные для показа в школах и других учебных заведениях. Практика показа учебных фильмов широко распространена на Западе и особенно в <a href="https://ru.wikipedia.org/wiki/%D0%A1%D0%A8%D0%90">США</a>. Существуют специальные телевизионные каналы, круглосуточно показывающие только образовательные и научно-популярные документальные фильмы (<a href="https://ru.wikipedia.org/wiki/24_DOC">24 DOC</a>, <a href="https://ru.wikipedia.org/wiki/365_%D0%B4%D0%BD%D0%B5%D0%B9_%D0%A2%D0%92">365 дней ТВ</a>, <a href="https://ru.wikipedia.org/w/index.php?title=%D0%9D%D0%B0%D1%83%D0%BA%D0%B0_2.0_(%D1%82%D0%B5%D0%BB%D0%B5%D0%BA%D0%B0%D0%BD%D0%B0%D0%BB)&action=edit&redlink=1">Наука 2.0</a> и др.)
