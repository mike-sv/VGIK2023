<h2>Особенности документального кино</h2>
<p style="text-align: justify;">
	 Документальное кино&nbsp;— это сложный жанр, подготовка и работа над которым занимает длительное время: отбирается жизненный и документальный материал, на основе которого создаётся <a href="https://ru.wikipedia.org/wiki/%D0%A1%D1%86%D0%B5%D0%BD%D0%B0%D1%80%D0%B8%D0%B9">сценарий</a>. Структура документального фильма многообразна: используются как постановочная, так и <a href="https://ru.wikipedia.org/wiki/%D0%A0%D0%B5%D0%BF%D0%BE%D1%80%D1%82%D0%B0%D0%B6">репортажная</a>
	съёмка, натурные и <a href="https://ru.wikipedia.org/wiki/%D0%98%D0%BD%D1%82%D0%B5%D1%80%D1%8C%D0%B5%D1%80">интерьерные</a>
	съёмки, архивные видео- и фотоматериалы.
</p>
<h2>Поджанры документального кино</h2>
<h3>
<ul>
	<li>Художественно-документальное кино<br>
 </li>
</ul>
 </h3>
<p style="text-align: justify;">
	 Художественно-документальное кино является смешанным жанром на основе документалистики. Большую часть таких фильмов составляют документальные кадры, но при этом часть отсутствующего или недоступного документального материала заменяется реконструкцией событий в исполнении профессиональных или непрофессиональных актёров. Ярким примером такого кино является знаменитый фильм <a href="https://ru.wikipedia.org/wiki/%D0%A0%D0%BE%D0%BC%D0%B0%D0%BD_%D0%9A%D0%B0%D1%80%D0%BC%D0%B5%D0%BD">Романа Кармена</a> «<a href="https://ru.wikipedia.org/wiki/%D0%92%D1%8C%D0%B5%D1%82%D0%BD%D0%B0%D0%BC_(%D1%84%D0%B8%D0%BB%D1%8C%D0%BC)">Вьетнам</a>», в котором из-за того, что <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%A0%D0%92">вьетнамские</a>
	власти опасались за безопасность советских кинооператоров, многие исторические сцены недавних боёв были сделаны постановочными. В этом случае в начальных титрах Роман Кармен честно указал, что фильм «художественно-документальный». В наше время художественно-документальных фильмов с игровой реконструкцией событий силами драматических актёров появляется всё больше, но современные <a href="https://ru.wikipedia.org/wiki/%D0%9A%D0%B8%D0%BD%D0%BE%D1%80%D0%B5%D0%B6%D0%B8%D1%81%D1%81%D1%91%D1%80">кинорежиссёры</a>
	уже не считают нужным утруждать себя предупреждением зрителя о наличии в «документальном» фильме постановочных кадров, что постепенно сближает такое кино с <a href="https://ru.wikipedia.org/wiki/%D0%9F%D1%81%D0%B5%D0%B2%D0%B4%D0%BE%D0%B4%D0%BE%D0%BA%D1%83%D0%BC%D0%B5%D0%BD%D1%82%D0%B0%D0%BB%D1%8C%D0%BD%D1%8B%D0%B9_%D1%84%D0%B8%D0%BB%D1%8C%D0%BC">псевдодокументалистикой</a>.
</p>
<h3>
<ul>
	<li>Псевдодокументальное кино<br>
 </li>
</ul>
 </h3>
<p>
 <a href="https://ru.wikipedia.org/wiki/%D0%9F%D1%81%D0%B5%D0%B2%D0%B4%D0%BE%D0%B4%D0%BE%D0%BA%D1%83%D0%BC%D0%B5%D0%BD%D1%82%D0%B0%D0%BB%D1%8C%D0%BD%D1%8B%D0%B9_%D1%84%D0%B8%D0%BB%D1%8C%D0%BC"></a>
</p>
<p style="text-align: justify;">
 <a href="https://ru.wikipedia.org/wiki/%D0%9F%D1%81%D0%B5%D0%B2%D0%B4%D0%BE%D0%B4%D0%BE%D0%BA%D1%83%D0%BC%D0%B5%D0%BD%D1%82%D0%B0%D0%BB%D1%8C%D0%BD%D1%8B%D0%B9_%D1%84%D0%B8%D0%BB%D1%8C%D0%BC">Псевдодокументальное кино</a> или т.&nbsp;н. «мокьюментари», описывает вымышленные события, выдавая их за действительные: например, телепередача <a href="https://ru.wikipedia.org/wiki/%D0%91%D0%B8-%D0%B1%D0%B8-%D1%81%D0%B8">BBC</a>
	об <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%B5%D1%80%D0%B5%D0%B2%D0%BE_%D1%81%D0%BF%D0%B0%D0%B3%D0%B5%D1%82%D1%82%D0%B8">урожае спагетти</a> в <a href="https://ru.wikipedia.org/wiki/%D0%A8%D0%B2%D0%B5%D0%B9%D1%86%D0%B0%D1%80%D0%B8%D1%8F">Швейцарии</a>, <a href="https://ru.wikipedia.org/wiki/%D0%A1%D0%A8%D0%90">американский</a>
	проект «<a href="https://ru.wikipedia.org/wiki/%D0%92%D0%B5%D0%B4%D1%8C%D0%BC%D0%B0_%D0%B8%D0%B7_%D0%91%D0%BB%D1%8D%D1%80">Ведьмы из Блэр</a>», российский фильм «<a href="https://ru.wikipedia.org/wiki/%D0%9F%D0%B5%D1%80%D0%B2%D1%8B%D0%B5_%D0%BD%D0%B0_%D0%9B%D1%83%D0%BD%D0%B5_(%D1%84%D0%B8%D0%BB%D1%8C%D0%BC)">Первые на Луне</a>». Фильмы этого жанра используют сюжетное построение и эстетику, подчёркнуто заимствованные из документального кино.
</p>
