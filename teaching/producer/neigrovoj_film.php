<p style="text-align: justify;">
	 Кафедра режиссуры неигрового фильма располагается в учебном корпусе по адресу: 129226, Москва, улица Вильгельма Пика, дом 3, кабинет 608 и 609 (6 этаж нового корпуса). Телефон: 8-499-760-23-87;&nbsp;<a href="mailto:vrej@vgik.info">vrej@vgik.info</a>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Кафедра неигрового фильма существует во ВГИКе с 1961 года. Среди документов, в архиве института читаем документ – «Приказ по Главному управлению учебных заведений и кадров Министерства Культуры РСФСР от 3 июня 1961 г. №20. Утвердить заведующим кафедрой кинорежиссуры документальной и научно-популярной кинематографии Всесоюзного государственного института кинематографии доцента Широкова Григория Павловича.»
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Но конечно же большой отряд выпускников постановочного факультета уже работал на документальных студиях страны. Среди них Э.Рязанов, З.Фомина, В.Катанян, Л.Дербышева, А.Косачев, Д.Фирсова, Д.Мусатова, А.Рыбакова. Работали на студиях и выпускники довоенных лет. В 1957 году была набрана специализированная мастерская документального кино под руководством народного артиста СССР Ильи Петровича Копалина. Через два года сразу две мастерские документального кино (руководитель Арша Амбарцумовна Ованесова) и научно-популярного кино (руководитель мастерской Александр Михайлович Згуриди). В 1960 году набрал мастерскую Роман Лазаревич Кармен. И вот приказом узаконено положение. Подробности становления кафедры мы попытались отразить в изданных ВГИКом книгах – «Мастерская И.П.Копалина», «Мастерская Арши Ованесовой и Леонида Кристи» и «Мастерская Романа Кармена». Книги находятся в библиотеке ВГИКа, интересующиеся периодом становления неигрового фильма в ВГИКе могут ознакомиться с подробностями. Кафедра получила новое название – неигрового фильма в годы «перестройки», вслед за переименованием жанра.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В 1971 году заведующим кафедрой становится Роман Кармен, в 1978 году – Александр Кочетков, в 1997 году – доверена кафедра Виктору Лисаковичу. Руководили мастерскими документального кино педагоги – И.П.Копалин, А.А.Ованесова, Л.М.Кристи, Р.Л.Кармен, А.С.Кочетков, Л.Н.Дербышева, Е.И. Вермишева, И.И.Гелейн, Т.А.Семенов, И.А.Григорьев, А.Ю.Ханютин. Долгие годы успешно работали мастерские научно-популярного и учебного кино под руководством А.М.Згуриди, Б.Г.Долина, Б.А.Альтшуллера, Е.С.Саканян, В.А.Манна, С.Л.Райтбурга, В.М.Кобрина
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Сегодня на кафедре работают пять мастерских неигрового фильма – С.В.Мирошниченко и С.Л.Зоновой, Б.Я.Рабиновича и Е.И.Резникова, А.Е.Учителя и А.И.Гелейна, В.П.Лисаковича и А.И.Гелейна, Осипова А.В. и Т.П.Юриной.
</p>
