<h3>
<ul>
	<li>Докудрама<br>
 </li>
</ul>
 </h3>
<p>
 <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%BE%D0%BA%D1%83%D0%B4%D1%80%D0%B0%D0%BC%D0%B0"></a>
</p>
<p style="text-align: justify;">
 <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%BE%D0%BA%D1%83%D0%B4%D1%80%D0%B0%D0%BC%D0%B0">Докудрама</a>
	или «документальная драма»&nbsp;— современный популярный «гибридный» <a href="https://ru.wikipedia.org/wiki/%D0%96%D0%B0%D0%BD%D1%80%D1%8B_%D0%B8%D0%B3%D1%80%D0%BE%D0%B2%D0%BE%D0%B3%D0%BE_%D0%BA%D0%B8%D0%BD%D0%BE">жанр игрового кино</a>, но не являющийся документальным по сути, делающий упор на воспроизведение исторических событий силами драматических актёров, но внешне подающийся в форме документального или научно-популярного фильма. Примерами докудрам являются популярные <a href="https://ru.wikipedia.org/wiki/%D0%98%D1%81%D1%82%D0%BE%D1%80%D0%B8%D1%87%D0%B5%D1%81%D0%BA%D0%B8%D0%B9_%D1%84%D0%B8%D0%BB%D1%8C%D0%BC">исторические</a>
	и историко-<a href="https://ru.wikipedia.org/wiki/%D0%A4%D0%B8%D0%BB%D1%8C%D0%BC-%D0%B1%D0%B8%D0%BE%D0%B3%D1%80%D0%B0%D1%84%D0%B8%D1%8F">биографические</a>
	сериалы <a href="https://ru.wikipedia.org/wiki/%D0%91%D0%B8-%D0%B1%D0%B8-%D1%81%D0%B8">BBC</a>, такие как «<a href="https://ru.wikipedia.org/wiki/%D0%94%D1%80%D0%B5%D0%B2%D0%BD%D0%B8%D0%B9_%D0%A0%D0%B8%D0%BC:_%D0%A0%D0%B0%D1%81%D1%86%D0%B2%D0%B5%D1%82_%D0%B8_%D0%BF%D0%B0%D0%B4%D0%B5%D0%BD%D0%B8%D0%B5_%D0%B8%D0%BC%D0%BF%D0%B5%D1%80%D0%B8%D0%B8">Древний Рим: Расцвет и падение империи</a>» (Ancient Rome: The Rise and Fall of an Empire) или «<a href="https://ru.wikipedia.org/w/index.php?title=%D0%92%D0%B5%D0%BB%D0%B8%D0%BA%D0%B8%D0%B5_%D0%B2%D0%BE%D0%B8%D0%BD%D1%8B:_%D0%93%D0%B5%D1%80%D0%BE%D0%B8_%D0%B8_%D0%B7%D0%BB%D0%BE%D0%B4%D0%B5%D0%B8&action=edit&redlink=1">Великие воины: Герои и злодеи</a>» (Warriors. Heroes and Villains).
